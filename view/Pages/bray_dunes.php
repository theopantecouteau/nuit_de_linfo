        <h4><br><strong>1959</strong></h4>



        <p>Les Hospitaliers Sauveteurs Bretons, autre société de sauvetage ayant des buts similaires à la Société Centrale de Sauvetage des Naufragés,&nbsp; fondent une section à Bray-Dunes et y organise un poste de secours.</p>



        <p>La section est présidée par M. Edmond Lerooy,&nbsp;capitaine de la marine marchande et adjoint au maire de la localité.<br>Dans son annuaire des sections on trouve&nbsp; :<br>Délégué départemental Nord M <strong>Goulven Le Bris,</strong> huissier, 53 rue&nbsp;Caumartin à Dunkerque.<br>Section de Bray-Dunes : président <strong>Edmond Lerooy</strong>, rue&nbsp;M. Clairin à Bray-Dunes</p>



        <h4><br>1966&nbsp;</h4>



        <p>Président M <strong>André Crepy</strong>, 8 rue Galliéni à Marcq-en -Bareuil et Villa Jeanne d’Art, Digue de&nbsp;mer, Bray-Dunes</p>



        <p>Poste de secours équipé d’un canot&nbsp;pneumatique de type Zodiac Mark II indicatif HSB 591</p>



        <h4><strong>1967&nbsp;</strong></h4>



        <p>Avec la fusion des Hospitaliers Sauveteurs Bretons et de la Société Centrale de Sauvetage des Naufragés la station braydunoise&nbsp;passe sous contrôle de la SNSM.</p>



