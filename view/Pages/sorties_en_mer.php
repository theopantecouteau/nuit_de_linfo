        <p><em>La présence de bancs de sable&nbsp;a toujours&nbsp;rendu la navigation périlleuse&nbsp; au large du dunkerquois.</em></p>



        <p><em>Si ces bancs ont rendu&nbsp;moult services à l’incontournable Jean Bart,&nbsp;ils sont aussi responsables de nombreux naufrages.</em></p>



        <p><em>Faisant abstraction des dangers, de vaillants marins ont tout fait afin d’en réduire les conséquences, sauvant un grand nombre de personne</em>s.</p>



        <p><em>Vous trouverez le récit de leurs exploits dans les sorties en mer&nbsp;recensées entre 1739 et 2021 dans l’état actuel des données.&nbsp;Dans ces textes, certaines tournures peuvent paraître archaïques , nous n’avons pas voulu les modifier pour garder la simplicité et l’authenticité de ces témoignages.</em></p>



        <p><em>On parle de sorties en mer car toutes ces missions ne se soldent pas forcément par un sauvetage. (refus d’être assisté, fausse alerte….)</em></p>



