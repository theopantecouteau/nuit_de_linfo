        <h2>Canon Perrier – 1870</h2>



        <p>Le canon porte-amarre, réalisé à partir d’un canon perrier réformé monté sur affût ou sur fourche. Ce canon pouvait lancer sa flèche de fer Delvigne à 225 m environ, selon la force et la direction du vent.</p>



