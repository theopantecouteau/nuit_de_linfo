        <h2>Mortier Mamby</h2>



        <p>Le canon porte-amarre monté sur affût, type Manby, portée 130 à 140m et sa variante, porté à l’épaule, désolidarisé de son affût ou brancard. Il comporte alors un dispositif amortisseur de recul (ressorts et caoutchouc), sa portée est de 100m environ</p>



        <p>Le mortier Manby est en bronze, du calibre de 14 centimètres, pesant 70 kilogrammes, lançant une bombe cylindro-sphérique de 15 kilogrammes, au moyen d’une charge de poudre de 200 à 400 grammes. Sa portée, avec une ligne de 9 millimètres, est de 200 mètres, mais la fréquence des ruptures fait employer de préférence une ligne de 10 millimètres de diamètre. La bombe porte à sa tête quatre fusées éclairantes ; l’attache de la corde est garnie de cuir, qu’il convient de mouiller avant le tir,&nbsp;afin d’éviter que les gaz de la poudre ne brûlent l’attache. Les inconvénients de ce système sont la difficulté que présente le transport de la pièce, le poids de la charge et celui du projectile, qui sont considérables, eu égard au résultat obtenu. Le canon Perrier lui sera préféré.</p>



