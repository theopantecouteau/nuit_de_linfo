        <h1 class="has-text-align-center">Le remorquage à Dunkerque</h1>



        <h5>Un démarrage tardif</h5>



        <p>En 1841 le Havre possède 13 remorqueurs à vapeur alors qu&rsquo;à Dunkerque chacun attendait ou vivait d’expédients.</p>



        <p>Après accord entre capitaines, un vapeur entrait au port ou en sortait avec en remorque un voilier.</p>



        <p>La ville, les armateurs ou la&nbsp;&nbsp;Chambre de Commerce ne se décident pas à créer un service de remorquage.</p>



        <h4>&nbsp;<strong>1859</strong></h4>



        <p>Gaspard Malo et son frère Célestin, armateur et constructeur de navires, exploitent le premier remorqueur à vapeur : l&rsquo;Orphéon.</p>



        <p>Un premier remorquage est effectué en avril 1859 pour un bateau de 15,5 pieds (environ 5 mètres) de tirant d’eau, retenu au bassin par les vents de nord-est.</p>



        <h5><strong>1861</strong></h5>



        <p>Le service du remorquage par bateaux à vapeur que la Chambre de Commerce a été autorisée à établir par une loi du 18 juin 1859, fonctionne depuis le 1er août 1861 ; on a déjà pu apprécier les avantages qui résultent de son application pour la fréquentation du port de Dunkerque.<br>L’exclusivité dont jouissait la Chambre provoqua l’abandon du remorquage par Gaspard Malo.</p>



        <h4><strong>1876</strong></h4>



        <p>La chambre de commerce de Dunkerque a mis à la disposition du Comité de Sauvetage ses remorqueurs pour faciliter la sortie du canot quand il aura à se porter au secours d&rsquo;un bâtiment en détresse.</p>



        <p>Les trois remorqueurs de Dunkerque ont été dotés d&rsquo;espingoles mises en batteries sur les bastingages et prêtes à envoyer une amarre à un navire en détresse.</p>



        <p>En réalité les remorqueurs participent effectivement aux actions de sauvetage depuis 1862.</p>



        <h5><strong>1882</strong></h5>



        <p>Les 4 remorqueurs sont équipés de postes porte-amarres et de secours.</p>



        <p>Le port est équipé de 30 lignes Torrès et 3 lignes Brunel.</p>



        <h5><strong>1897</strong></h5>



        <p>Le port de Dunkerque perd le monopole du remorquage.</p>



        <p>La Société Dunkerquoise de Remorquage Barra et Cie est créée.</p>



        <p>Elle est considérée comme fondatrice du remorquage professionnel à Dunkerque.</p>



        <p>Arrivée du remorqueur Commerce 3.</p>



        <h4>1899</h4>



        <p>La Société Dunkerquoise de Remorquage Barra et Cie devient la Société Dunkerquoise de Remorquage et de Sauvetage. (SDRS)</p>



        <p>Les noms donnés aux remorqueurs sont pris dans les noms donnés par Jean Bart à ses bateaux ou à ses corsaires &#8211; Le Hardi &#8211; Le Triomphant&#8230;</p>



        <h4>1902</h4>



        <p>Absorption  de l&rsquo;armement Rooryck.</p>



        <h4>1956</h4>



        <p>La SDRS&nbsp;devient&nbsp; la&nbsp;Société de Remorquage et de Sauvetage du Nord.</p>



        <p>Un changement de nom qui s’accompagne de la constitution d’une flotte plus moderne et plus puissante, dont le navire amiral est sans conteste le&nbsp;« Jean Bart »&nbsp;qui était affecté au sauvetage et au remorquage hauturier.</p>



