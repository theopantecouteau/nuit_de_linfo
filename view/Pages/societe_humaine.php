        <h1 class="has-text-align-center"><strong>La</strong> <strong>Société Humaine de Dunkerque</strong></h1>



        <h2 class="has-text-align-center">40 ans de sauvetage</h2>



        <blockquote class="wp-block-quote"><p><strong>En France, il est plus aisé d’improviser une révolution qui bouleverse en quelques heures tout l’ordre social, que de fonder un établissement philanthropique, destiné à encourager aux véritables sentiments de la fraternité humaine.</strong></p><p><em>Jean Joseph Carlier dit «&nbsp;Carlier l’Aîné&nbsp;» Secrétaire et fondateur de la Société Humaine&nbsp;1794 – 1874</em></p></blockquote>



        <h5><strong>L’acte fédérateur</strong></h5>



        <p>Le mard<strong>i 28 décembre 1810,</strong>&nbsp;une tempête effroyable se déchaînait sur les parages de Dunkerque, et un trois-mâts anglais de la Compagnie des Indes, <a href="?controller=site&action=redirect&page=">Queen Elizabeth</a>, s’échouait sur les bancs qui ferment la rade. Deux embarcations du bord, portant vingt-deux personnes, vinrent atterrir au Fort-Risban, où les recueillirent les canonniers garde-côtes ; mais la violence du temps paralysa toutes les tentatives de secours et pendant tout le jour on put voir, des remparts de la ville, des malheureux appendus aux vergues et aux huniers, qui disparaissaient tour à tour dans les brisants d’une mer furieuse.</p>



        <p><strong>Trois cent cinquante-huit cadavres</strong>&nbsp;de tout âge et de tout sexe, blancs, nègres, lascars, furent jetés à la côte, avec les débris d’une riche cargaison de produits européens que l’Elizabeth transportait au Ben­gale.</p>



        <p>Ce fut une profonde consternation par toute la ville. A ce moment même était logée à l’Hôtel d’Angleterre, aujourd’hui la salle Sainte-Cécile, une famille américaine du nom d’Armstrong, composée d’un vieux père, de la mère et d’une jeune demoiselle. Ces voyageurs étaient venus à Dunkerque, attendre l’occasion de quelque navire neutre, pour retourner en Amérique, passant par l’Angleterre. Le naufrage de l’Elizabeth accabla ces étrangers d’une indicible douleur, et les familles respectables de la ville, auxquelles ils avaient été recommandés, s’empressaient à leur porter des consolations.</p>



        <p>Ce fut là que&nbsp;<strong>Jean Joseph Carlier&nbsp;</strong>rencontra&nbsp;la première fois <a href="?controller=site&action=redirect&page="><strong>Benjamin Morel</strong></a>, et qu’ils sympathisèrent.&nbsp;<strong>Benjamin&nbsp; Morel&nbsp;</strong>exprimait avec un&nbsp;tact de bon goût, avec un affectueux langage à ces étrangers attristés, les sentiments d’une ingénieuse commisération.<br>En se racontant les détails affligeants de la perte de l’Elizabeth, ils&nbsp; échangent déjà&nbsp; les idées que leur&nbsp;inspirait un sentiment commun de regret à voir que les entrées des ports de mer, où des scènes pareilles se renouvellent fréquemment, fussent dépourvues de tout moyen spécial de sauvetage.</p>



        <h5><strong>Prospection et études préalables</strong></h5>



        <p>Toutes ces circonstances amassées dans leur esprit, depuis de longues années, les ont porté à s’enquérir expressément, dans un voyage qu’ils firent&nbsp;&nbsp;au printemps de 1830, allant visiter les ports de mer de l’Angleterre et de l’Ecosse, des mesures déjà mises en usage dans ce but, sur les divers points des côtes de ce pays, essentiellement livré à la pratique et à l’étude de la navigation.</p>



        <p>Ils prirent connaissance, à Newcastle et à Sunderland, de la façon dont étaient construits, aménagés et manœuvrés les life-boats établis dans les ports du Northumberland et du Yorkshire. Ils examinèrent&nbsp; à Londres, sous les auspices de l’honorable M. Larking, président de la Humane Society, dont le siège est établi sur les bords de la Serpentine de Hide-Park, les appareils qui préservent annuellement d’asphyxie un immense nombre d’imprudents qui se livrent aux plaisirs de la natation ou du patinage.</p>



        <p>Les événements qui survinrent en 1830, peu de temps après ce voyage, les forcèrent cependant d’ajourner les projets qu’ils se proposaient de réaliser, dans le but si souvent désiré entre nous.</p>



        <p>Après avoir forcément attendu l’apaisement des orages politiques, ils se mirent&nbsp;de nouveau en quête des moyens propres à atténuer l’effet des fléaux météorologiques.</p>



        <p>En 1833, ils visitèrent&nbsp;&nbsp;<strong>la Société Humaine de Boulogne-sur-Mer</strong>, qui venait de rendre des services signalés, lors du naufrage de l’Amphytrite où 400 femmes, que l’on transportait en Australie, périrent victimes des éléments.</p>



        <p>Ils retrouvèrent&nbsp;leur&nbsp;ancien ami, le sous-commissaire de marine de Dunkerque, M. Michelin, qui leur&nbsp;fit tout voir, et les renseigna sur tout ce qu’il leur était nécessaire de savoir pour l’organisation d’une pareille institution</p>



        <h5><strong>Naissance de la société humaine</strong></h5>



        <p>Revenus à Dunkerque, ils modélisent les&nbsp;&nbsp;statuts de la Société Humaine de Dunkerque&nbsp;sur ceux de Boulogne, et furent&nbsp;assez heureux pour les mettre sous le patronage de tout ce qu’il y avait de respectables notabilités, Dames et Messieurs, de la ville. Les statuts furent imprimés dès le mois de&nbsp;<strong>janvier 1834.</strong></p>



        <p><strong>16 mai 1834</strong></p>



        <p>La société est autorisée&nbsp; elle a pour objet&nbsp;:</p>



        <p>«&nbsp;Secourir les noyés et les naufragés dans le rayon le plus étendu que le permettent les ressources de la société&nbsp;»</p>



        <p>Les sociétaires donnent des cours de natation et assurent la protection des baigneurs des bains de mer. Ils recherchent et enseignent les nouvelles techniques de sauvetage pour les mettre en œuvre.</p>



        <p>Etaient membres de cette société toutes les personnes souscrivant pour une somme de 5 francs au moins pendant trois ans.&nbsp;»</p>



        <p><strong>15 juillet 1834&nbsp;</strong>Les statuts sont déposés par&nbsp;Jean Joseph Carlier&nbsp; et Benjamin Morel qui en accepta la présidence.</p>



