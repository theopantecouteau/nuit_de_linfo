        <h4>40 ans de&nbsp; société humaine de Dunkerque</h4>



        <h5>&nbsp;<strong>7 mai 1835 – Moyens matériels</strong></h5>



        <p>Le maire de la ville demande au Ministre de la Marine et des Colonies de mettre à disposition de la SH un canot&nbsp;de sauvetage insubmersible construit par le gouvernement à Cherbourg. La demande est acceptée. Conduit à la mer à l’aide d’un chariot, il&nbsp;effectue sa première sortie le&nbsp;<strong>29 novembre 1836</strong>. Commandé par le patron Caron, il se porte au secours du voilier la « <a href="?controller=site&action=redirect&page=">JEUNE MARIE CHARLOTTE</a> ».</p>



        <p>En 1835 la Société Humaine crée deux établissements à l’est&nbsp;<strong>(A)</strong>&nbsp;et à l’ouest&nbsp;<strong>(B)</strong>&nbsp;du chenal.&nbsp;Les deux établissements sont équipés de matériel médical et&nbsp;nautique.&nbsp;Un hangar construit à coté de l’établissement de l’est abrite le canot monté sur son chariot.&nbsp;Les poulies, ancres, grelins et cordage y sont stockés.</p>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="834" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Carte-Dunkerque-emplacement-des-locaux-SH-1024x834.png" alt="Carte Dunkerque emplacement des locaux SH" class="wp-image-2030"><figcaption><strong>Carte Dunkerque emplacement des locaux SH</strong><br>La localisation&nbsp; des établissements est une estimation. Les archives de la SH font mention de difficultés administratives pour arriver à&nbsp; les construire en dehors des remparts de la ville. La localisation de l’établissement A est cohérent avec le futur abri qui sera construit par la SCSN</figcaption></figure>



        <h5><strong>18 mars 1942 –  Remerciements</strong></h5>



        <p><br>Dans sa séance dernière, la Société Humaine de Dunkerque a décerné à Jean-Baptiste-François Bense, de Calais, âgé de &nbsp;douze ans, une médaille en argent de grand modèle, en récompense du bel acte de dévouement par lequel cet enfant s’est distingué le 18 avril 1841, en sauvant, dans le bassin de la Marine, un autre enfant de 8 ans, de cette ville. Le jeune Maes, jouant dans le canot d’un navire commandé par le capitaine Bense, tomba dans le bassin. Aussitôt le fils de ce capitaine, malgré la température encore froide, se précipita, tout habillé, plongea et ramena sur l’eau le petit malheureux qui se noyait. Bense nagea vers son embarcation, mais celle-ci était trop haute de bordée pour qu’il pût mettre la main sur le plat-bord sans laisser échapper sou précieux fardeau. Bientôt il sent ses &nbsp;forces s’épuiser par l’inutilité de ses efforts, et sa position devenait tellement critique, que tout autre à sa place eût cherché à se sauver d’abord, en abandonnant celui qu’il peut à peine soutenir; mais Bense, doué d’une énergie au-dessus de son âge, veut sauver l’enfant ou périr ou avec lui. Réunissant ses forces et redoublant d’efforts, il nage d’un bras, tenant toujours de l’autre Maes évanoui, et cherche à gagner une prochaine échelle. Ce fut alors que l’on put leur venir en aide et sauver l’un et l’autre.</p>



        <h5><strong>1856 – Philanthropie</strong></h5>



        <p><em>Elle n’a pas cessé depuis lors de mériter les plus louables éloges. Le dernier acte auquel nous-mêmes nous coopérâmes, fut la souscription dont la Société Humaine prit l’initiative en faveur des familles des 157 marins péris à Islande dans la campagne de 1856.</em></p>



        <p><em>Sans doute, en qualité de secrétaire de la Société Humaine, nous prîmes à cette honorable manifestation la part que l’intérêt de la cause réclamait de notre zèle ; mais, nous devons l’avouer hautement, le dévouement et l’influence personnelle de M. Morel furent les auxiliaires les plus puissants qui en assurèrent le succès.</em></p>



        <p><em>Comprenant les grandes souscriptions du Roi, des ministres, du comte Roger, député, du Préfet du Nord, de Monseigneur l’évêque de Cambrai, nous eûmes le bonheur de réunir une somme de 56 000 francs, qui nous fut fournie par les libérales offrandes de villes et communes.</em></p>



        <h5><strong>Benjamin Morel&nbsp; – Un président actif et présent.</strong></h5>



        <p><em>&nbsp;29 novembre 1856&nbsp;– <u>Echouement de la Jeune-Marie- Charlotte.</u></em><br>&nbsp;</p>



        <p><em>La presse locale relate&nbsp; que le président de la Société s’était rendu des premiers au poste de la baraque, où était déposé le bateau sauveteur, et qu’il ne cessa d’y donner les ordres nécessaires à la circonstance.</em></p>



        <p><u><em><a href="?controller=site&action=redirect&page=">1848 Naufrage du Porc Epic</a></em></u></p>



        <p><em>La&nbsp;généreuse conduite de&nbsp;Benjamin Morel, dans le sauvetage des naufragés eut assez de retentissement pour que le Ministre de la Marine et des Colonies lui en adresse une lettre particulière de sincère gratitude, au nom du corps de la marine qu’il représente.</em></p>



        <p><em>7 décembre 1855 – Naufrage des Dorothy et Diana</em><br>&nbsp;</p>



        <p><em>M. Morel, accompagné des membres du comité, se porta sur le lieu du sinistre et y présida pendant plusieurs heures à toutes les mesures utiles au sauvetage de ces bâtiments. Il ne quitta qu’après s’être assuré que tous les secours avaient été donnés aux équipages naufragés. En témoignage de reconnaissance pour cet énergique dévouement, malgré son grand âge, dit le journal la Dunkerquoise,<u><strong>&nbsp;M. Morel était alors âgé de 75 ans,</strong></u>&nbsp;le principal secrétaire d’Etat de Sa Majesté britannique écrivit à M. Morel une lettre officielle de félicitation</em></p>



        <p><em>29 janvier 1857- Naufrage des Trois Sœurs</em></p>



        <p><em>C’est dans ce drame&nbsp;&nbsp;où la mer furieuse se fit l’horrible part de cinq victimes, parmi lesquelles la cité eut à pleurer le brave pilote Mathias Bommelaer,&nbsp; l’intrépide vieillard Gaspard Neuts&nbsp;</em>capitaine de navire de pêche<em>&nbsp;et le jeune pilote Celle, que&nbsp;&nbsp;M<strong>. </strong>Conseil<strong>,</strong>&nbsp;ancien capitaine de port à Dunkerque écrit et raconte le zèle incessant de M. Benjamin Morel dans ces crises désastreuses. On y verra M. Morel présent sur les lieux, pendant tout le temps du sinistre, il prodigue à tous, blessés et naufragés, les soins les plus affectueux.</em></p>



        <p><em>Son devoir ne s’arrêtait pas pour lui à ces faits matériels ; quelques jours plus tard, M. Morel accompagnait, en tête des membres de la Société Humaine, le cortège funèbre des malheureux péris dans cette funeste journée. On l’y voyait, le deuil au cœur, suivi de toute une population attendrie, qui n’avait que des élans d’admiration pour un courage aussi sublime.</em></p>



        <p><em><u>1860 janvier ou février –</u>&nbsp;Nous n’avons&nbsp; pas de trace de&nbsp; naufrage dans ce moment signalé par Carlier l’Ainé dans sa biographie de B. Morel</em>.</p>



        <p><em>A la sortie du spectacle, au temps le plus rude de l’hiver (dans l’année même de la mort de M. Morel), on vint le prévenir que plusieurs navires étaient menacés de se perdre à l’est du port. La nuit était obscure, le froid intense ; M. Morel, n’écoutant que son cœur, se rendit sur la jetée, où il dirigea avec d’autres membres de la Société Humaine tous les moyens de sauvetage. Il ne quitta les lieux qu’après s’être assuré que la vie de tous était sauve. Il avait passé la nuit entière à essuyer la pluie et la grêle, ne résistant aux efforts de la tempête qu’en se cramponnant aux barres de l’estacade.</em></p>



        <h5><strong>Les premières années</strong></h5>



        <p>Ainsi que le mentionne le compte-rendu des travaux de la première année, rien n’avait été accompli efficacement au point de vue des sauvetages. La&nbsp;première année s’était passée tout en démarches, afin d’obtenir à la Société l’attache administrative, afin d’obtenir surtout l’autorisation d’établir des lieux de dépôts pour le matériel, en dehors des fortifications.</p>



        <p>ll fallut acheter et faire confectionner les objets nécessaires, et avant tout solliciter de toutes parts les souscripteurs, que nous eûmes la bonne chance d’attacher pour plusieurs années à raison de cinq francs par an, et de façon à produire une somme d’environ trois mille francs, comme dotation annuelle pendant les premières années, naturellement les plus coûteuses, à cause des frais d’installation.</p>



        <p>Le compte-rendu des travaux de la seconde année prit déjà de l’extension par l’importance des faits auxquels la Société avait apporté son secours. Il mentionnait l’encaisse de 4 000 francs, et, sous l’impulsion active de son président, admirablement secondé par tous les membres du Comité, la Société Humaine était toute prête à accomplir partout le but de son institution.</p>



        <h5><strong>1869</strong></h5>



        <p>La Société Humaine de Dunkerque s’est adressée à la Société Centrale de Sauvetage des Naufragés et a réclamé son concours pour l’aider à organiser sur de nouvelles bases le service du sauvetage, qui jusqu’ici a laissé beaucoup à désirer. Il n’existe à Dunkerque qu’un bateau de sauvetage ancien, dépourvu des propriétés les plus essentielles et des moyens propres à assurer son fonctionnement. La première chose à faire sera de doter cette station d’un canot de sauvetage, le Comité se propose de donner cette affectation au canot primitivement destiné à L’Herbaudière.</p>



        <h5><strong>1872</strong></h5>



        <p>La station de Dunkerque a été annexée à la Société Centrale en mettant à sa disposition son matériel et la somme de 14,000 francs, qu’elle avait en réserve. Son Comité a adhéré avec empressement au désir exprimé par MM. Marc Dessurne président de la Société et Plocq, ingénieur en chef des Ponts et Chaussées, son secrétaire.</p>



        <h5><strong>Lundi 30 novembre 1874</strong></h5>



        <p>Dernière assemblée générale de la Société Humaine de Dunkerque&nbsp;sous la présidence de&nbsp;Marc&nbsp;Dessurne.</p>



        <p>Le manque de ressources disponibles, financières&nbsp; formation etc. pour assurer correctement les services rendus pour le sauvetage en mer à Dunkerque amène le conseil d’administration à se dissoudre au profit de la&nbsp;<strong>S</strong>ociété&nbsp;<strong>C</strong>entrale de&nbsp;<strong>S</strong>auvetage des&nbsp;<strong>N</strong>aufragés à qui elle demande d’assurer dorénavant les missions de sauvetage.</p>



        <p>La surveillance des bains de mer est transférée à une organisation municipale.</p>



        <p>Le comité d’administration de la SCSN du 9 octobre 1874 avalise la demande de la SH. Elle dépêche son inspecteur général afin de prendre toutes les mesures nécessaires à la transition.</p>



        <p>Les établissements,&nbsp;matériels&nbsp; et&nbsp;mobiliers de la SH sont transférés à la SCSN.</p>



        <p>Les 14 000 francs qui restent au bilan de la Société Humaine sont&nbsp; transférés à la SCSN.</p>



        <p>Le SH devient bienfaitrice de la SCSN.</p>



