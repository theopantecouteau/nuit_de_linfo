        <h1 class="has-text-align-center"><strong>Le fusil porte amarres&nbsp;&nbsp; – Article de 1866</strong></h1>



        <p>​</p>



        <figure class="wp-block-image size-full"><img src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Fleche-porte-amarre.png" alt="Flèche porte-amarre" class="wp-image-2195"><figcaption>Flèche porte-amarre</figcaption></figure>



        <p>​</p>



        <p>​</p>



        <p>M. Delvigne, membre du Conseil d’administration de la Société centrale, bien connu déjà pour ses travaux antérieurs concernant les porte-amarres, travaux qu’il venait de reprendre sur la demande du Comité, reçut la mission d’expérimenter l’emploi du mousqueton comme engin de secours.</p>



        <p>​</p>



        <p>M. Delvigne fit observer tout d’abord qu’il lui paraissait indispensable de substituer à la baguette en fer du mousqueton une baguette en bois, disposée de manière à permettre d’amarrer la ligne en évitant les chances de rupture. D’après ces données, une série d’expériences furent faites au polygone de Vincennes, où le Comité d’administration se transporta le 16 octobre, afin de juger par lui-même des résultats obtenus.</p>



        <p>​</p>



        <p>A la suite de ces expériences, le Comité décida que plusieurs stations seraient immédiatement pourvues, à titre d’essai, de baguettes, de lignes et d’appareils va-et-vient, ou simplement d’amarres, suivant les localités. On désigna les jetées de Dunkerque, de Calais, de Boulogne, de Dieppe, du Havre, de Honfleur, de Port-en-Bessin, de Granville, de Camaret , deux postes de douane dans les passes d’Arcachon, un poste à l’embouchure de l’Adour.</p>



        <p>​</p>



        <p>Diverses circonstances, au nombre desquelles il faut compter les changements apportés récemment dans le service des douanes sur le littoral, ont retardé jusqu’ici les préparatifs de cette installation. L’expérience n’a donc pas encore prononcé sur la valeur pratique de ces engins. Dès à présent, cependant, il est indubitable que dans beaucoup de cas ils peuvent rendre des services, et comme partout où il existe un fusil, un mousqueton ou un fusil de chasse, ce système, très simple d’ailleurs, peut être employé, on nous saura gré de publier les extraits suivants d’un mémoire remis au Comité par M. Delvigne.</p>



        <p>​</p>



        <p>L’arme placée entre les mains des préposés des douanes est le mousqueton de gendarmerie. Son poids est.de 3,280 Kg., la longueur du canon de 76 centimètres, son calibre de 17 mm sa charge de poudre réglementaire de 4 gr. La charge d’épreuve de son canon de 22 gr. et 18 gr.</p>



        <p>La flèche porte-amarre est en bois de frêne rond de 13 millimètres de diamètre, et de 90 centimètres de longueur. L’extrémité qui porte sur la charge est garnie d’une virole en cuivre de 3 centimètres de longueur et de 17 mm de diamètre solidement fixée par deux goupilles en fer.</p>



        <p>​</p>



        <h5>INSTRUCTION SUR LE TIR DES FLÈCHES PORTE-AMARRES.</h5>



        <p>1° S’assurer si la ligne est bien disposée pour se développer.</p>



        <p>2° Examiner si les attaches et le coulant de la flèche serrent convenablement, les resserrer s’il est utile, les pousser contre la virole antérieure et graisser le bois de la flèche au besoin.</p>



        <p>3° Brûler une capsule, ou flamber même, par précaution.</p>



        <p>4° Verser dans le mousqueton 2 à 2 1/2 grammes de poudre.</p>



        <p>5° Introduire une bourre ou le sabot en carton, l’évasement du côté de la poudre et puis placer la flèche dans l’arme.</p>



        <p>6° Passer la ligne en deux ou trois passes dans les quatre boucles de l’attache, et relier son bout par deux demi-nœuds coulants.</p>



        <p>7° Placer la pelote de ligne à terre, ou la porter en sautoir dans sa boîte.</p>



        <p>8° Pointer le mousqueton sous 15 à 25 degrés au-dessus du point à. atteindre, si on est dans la direction du vent ou bien à droite ou à gauche d’une quantité proportionnée à sa force et à sa direction.</p>



        <p>9° S’il y a lieu de faire entraîner par la flèche le chapelet de bouchons, on attache le bout de corde qui les porte à l’extrémité de la ligne pelotée ; on place les bouchons, tous en ligne droite devant soi, on ramène en arrière l’autre bout de la corde qui les porte, et on la passe et fixe dans les boucles de l’attache.</p>



        <p>10° Faire sécher les lignes, les détordre au besoin et les repeloter.</p>



        <p>​</p>



        <p>Extraits de la suite de l’article/..</p>



        <p>​</p>



        <p>Pour le fusil d’infanterie, par exemple, dont le canon a 1 mètre 3 centimètres de longueur, il faut porter la longueur de la flèche à 1 mètre 10 centimètres environ.</p>



        <p>Un fusil de chasse, une baguette de bois disposée à la hâte et une pelote de bonne ficelle se dévidant de l’intérieur, pourraient déjà rendre de grands services dans les inondations et dans les cas d’incendie.</p>



        <p>Au besoin le tireur-sauveteur pourrait lancer deux à trois flèches en une minute.</p>



        <p>Tout étant prêt pour tirer, le sauveteur pointe le mousqueton sous 15 à 25 degrés d’inclinaison au-dessus de l’objet à atteindre, s’il est dans la direction du vent ; à droite ou à gauche en raison des déviations prévues, si le vent vient de côté, et il fait feu.</p>



        <p>II est indispensable de bien se pénétrer ici du phénomène qui se passe pendant le tir, afin d’assurer toujours les conditions nécessaires, puisque tout le système repose entièrement sur sa réalisation.</p>



        <p>A l’instant même où la flèche se met en mouvement, les attaches glissant le long du bois à frottement dur jusqu’au ressaut de la virole placée à l’arrière, entraînent la ligne, dont l’inertie se trouve graduellement vaincue.</p>



        <p>Si cette condition du glissement à frottement dur n’était pas réalisée, le choc briserait infailliblement les attaches. Il faut donc, avant de tirer, s’assurer qu’une assez forte pression de la main est nécessaire pour faire descendre et monter les boucles. Si le frottement n’est pas suffisamment fort, on ajoute quelques tours de ligature, et on refait un autre coulant. Il est toujours utile et quelquefois nécessaire de graisser légèrement le bois de la flèche, afin de favoriser le glissement.</p>



        <p>Quand par la pluie les bagues des boucles et surtout le coulant se trouvent trop serrés, on refait ce dernier pour que la résistance ne soit pas par trop forte et on graisse bien la flèche.</p>



        <p>Lorsqu’il s’agit de lancer une flèche pour porter secours ou pour établir une communication, la première règle à observer est, autant que possible, de tirer dans la direction du vent et d’éviter le vent de travers, quitte à augmenter un peu la distance à parcourir. Par une forte brise de travers, la ligne est toujours emportée et rejetée loin en dehors de la direction du tir ; la flèche subit aussi jusqu’à un certain point cette influence, mais il est à remarquer que sa déviation est loin d’être aussi grande que celle des projectiles sphériques, par cette raison que l’action de la ligne sur l’arrière de la flèche faisant incliner sa tête en sens opposé, elle pique dans le vent et dérive moins qu’on ne pourrait croire.</p>



        <p>Sans nul doute, il eût été désirable d’employer des flèches pouvant flotter ; mais devant être lancées par un mousqueton ou par un fusil, de pareilles flèches seraient trop légères pour entraîner une ligne d’une résistance suffisante.</p>



        <p>Le dessin reproduit à l’appui de cette note et les renseignements qui l’accompagnent donnent une idée exacte des flèches de M. Delvigne, de leurs accessoires et des caisses destinées à les contenir. On trouvera parmi les documents, dans le compte rendu de la séance du 16 octobre dernier, le procès-verbal des expériences faites à Vincennes. Depuis cette époque on est parvenu à accroître la portée des flèches jusqu’à 90 mètres.</p>



        <p>​</p>



        <h5>OBJETS CONTENUS DANS LA CAISSE DE FLÈCHES PORTE-AMARRES DE SAUVETAGE.</h5>



        <p>1° Six flèches pourvues de leurs attaches et coulants .</p>



        <p>2° Quatre pelotes de lignes de cent mètres de long</p>



        <p>3» Une pelote de vingt mètres de cordeau pour attaches.</p>



        <p>4° Une petite pelote portant 12 bouchons pour flotter</p>



        <p>5° Une petite pelote de fil goudronné pour les ligatures.</p>



        <p>6° Deux mandrins coniques pour peloter les lignes.</p>



        <p>7° Une boîte pour porter la pelote en sautoir.</p>



        <p>8» Deux mesures pour la poudre, l’une pour 2 grammes, l’autre 2 ½.</p>



        <p>9° Deux petits mandrins en bois pour rouler les cartouches.</p>



        <p>10° Deux petites boîtes en fer-blanc pour la poudre et pour la graisse.</p>



        <p>11° Un bout de cylindre en cuivre, à engager sur la virole antérieure de la flèche, pour servir de moule à couler le lingot. .</p>



        <p>12° Une cuiller à fondre le plomb.</p>



        <p>13° Vingt-quatre culots en carton embouti .</p>



