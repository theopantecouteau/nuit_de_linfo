        <h1 class="has-text-align-center">La gaffe Legrand</h1>



        <blockquote class="wp-block-quote"><p>M. <strong>Jules Legrand</strong>, courtier au Havre, a imaginé une sorte de gaffe permettant non plus seulement de lancer une bouée munie d’une corde à l’homme qui se noie, mais encore de l’accrocher et d’aller le chercher même sous l’eau.</p></blockquote>



        <figure class="wp-block-image size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Gaffe-Legrand.png" alt="Gaffe Legrand" class="wp-image-2127" width="836" height="172" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Gaffe-Legrand.png 679w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Gaffe-Legrand-300x62.png 300w" sizes="(max-width: 836px) 100vw, 836px"></figure>



        <p>J’ai été frappé, écrit M. Jules Legrand, de l’insuffisance des moyens de sauvetage employés pour secourir les individus qui tombent à l’eau car sans chercher à méconnaître les services rendus par les bouées, et particulièrement par la ligne système Torrès, ces engins sont malheureusement impuissants dans la plupart des accidents.</p>



        <p>Cela tient aux diverses circonstances indispensables pour que leur emploi réussisse.« Il faut d’abord que l’individu tombé à l’eau ne soit pas ivre, or c’est très-souvent le cas. Il faut, en second lieu, qu’il conserve assez de sang-froid et de présence d’esprit pour saisir la ligne ou la bouée qui lui est lancée, condition bien rare chez un individu sur le point de se noyer, suffoqué, aveuglé, et qui, la plupart du temps, se trouve entre deux eaux quand les secours arrivent à lui…</p>



        <p>J’ai donc cherché à établir un appareil, de telle sorte que tous ces inconvénients disparaissent.</p>



        <p>​Cet appareil se compose d’une gaule en sapin de Norvège d’environ 6 mètres, à la fois très-légère et très-solide, au bout de laquelle se trouve emmanché une espèce de grappin en fer à trois branches de 25 centimètres terminées chacune par une sorte de fourchette aux extrémités assez arrondies pour ne pas blesser. « A environ 1 mètre de l’extrémité du manche, est placée une barre transversale en frêne, de 40 centimètres, assez forte pour permettre de s’y tenir à deux mains, et même de se mettre à cheval dessus ; chacune des extrémités de cette barre est, en outre, munie d’un bout de ligne double terminé par une petite bouée : la ligne est double afin que l’individu qu’il s’agit de sauver puisse, en cas de besoin se la passer autour du bras ou du corps, s’il sent les forces lui manquer, en attendant qu’on puisse le ramener à terre.</p>



        <p>​Je suppose maintenant un homme à l’eau.</p>



        <p>S’il conserve assez de sang-froid et de présence d’esprit pour profiter des moyens actuels, le manche de la gaffe, la barre transversale, le grappin à trois branches, les deux bouts de lignes garnies de bouées lui suffiront amplement pour se sauver, d’autant plus qu’on pourra pour ainsi dire lui mettre l’appareil dans la main.</p>



        <p>Avantage qui n’existe pas avec les bouées ou lignes réclamant encore une certaine habileté de la part de ceux qui les lancent.</p>



        <p>​</p>



        <p>Dans le cas contraire, fût-il à demi mort de peur ou de saisissement, fût-il même coulé entre deux eaux, l’extrême légèreté de cette gaffe, la rigidité du manche, permettront d’engager les pattes bifurquées du trident dans ses vêtements et de le maintenir hors de l’eau sans qu’il s’aide en aucune façon, on pourrait presque dire malgré lui.</p>



        <p>En outre, et pour plus de sécurité, un fort fil de cuivre rouge (le cuivre jaune ne vaudrait rien) éprouvé à 200 kilogrammes, attaché à la partie la plus forte du grappin, descend tout le long du manche, et y est appliqué par des crampons en fer distancés d’environ 20 à 25 centimètres : le manche viendrait-il à casser par suite de la façon maladroite dont on se servirait de l’appareil, que les deux parties de la gaule resteraient unies et assureraient encore le sauvetage.</p>



