                <p><strong>Galiote</strong></p>



                <p>Une galiote désigne un type de navire de commerce, à voile, hollandais, de 50 à 300 tonneaux, autant arrondi à l&rsquo;avant qu&rsquo;à l&rsquo;arrière. Elles possédaient un fond à peu près plat, permettant de naviguer dans des eaux peu profondes. Les galiotes faisaient du cabotage en Mer du Nord et en mer Baltique. Pour éviter de trop dériver, à cause de leur fond plat, les galiotes portaient fréquemment une, ou deux, dérives latérales &#8211; <em><sub>Wikipédia</sub></em></p>
