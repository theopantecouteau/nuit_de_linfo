                <p class="has-text-align-center"><strong>Bélandre dunkerquoise</strong></p>



                <p>La Bélandre est proche de la famille des bateaux hollandais, comme le Tjalk du bassin de l’Escaut. gréée en sloop destinée au cabotage le long des côtes de la mer du Nord et de la Manche. Nom donné aux péniches dans la région de Dunkerque et Calais. Elle&nbsp;apparaît un peu comme une caricature de la péniche, avec laquelle elle a des ancêtres flamands et néerlandais communs. En effet, ses formes sont tellement exagérées, la courbure de ses épaulures tellement puissante, que les bordés s’incurvent jusqu’à entrer dans la coque au niveau de la quille. On imagine les tensions auxquelles est soumis le bois pour être ainsi ployé. Les dimensions et performances en terme de fret de la bélandre ne diffèrent guère de celles de la péniche flamande : 38,50 m à 39 m de long, 5,05 m de large, enfoncement en canal 1,80 m pour un fret de 250 à 280 tonnes, enfoncement en rivière 2,50 m pour un fret qui peut dépasser les 350 tonnes.</p>



                <p><em>Source :Musée de la batellerie</em></p>
