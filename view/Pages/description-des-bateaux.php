                <p><strong>CANOT LAMANEUR</strong></p>



                <p>Ce sont des canots lestés , propulsés à l&rsquo;aviron , mais aussi capables de porter une voile au tiers de 24 m². En principe, les lamaneurs s&rsquo;occupent de l&rsquo;amarrage des bateaux à quai , mais en réalité ils vont quelquefois offrir des services de pilotage assez loin en mer , paraît-il jusqu&rsquo;à 20 milles !.</p>



                <p>Ils leur arrive également d&rsquo;assurer le transport des passagers quand le bateau ne peut entrer pour cause de marée basse.</p>
