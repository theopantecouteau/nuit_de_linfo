        <h1 class="has-text-align-center">Technique de sauvetage Le va-et-vient</h1>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="620" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Technique-Va-et-vient-vu-globale-1024x620.jpg" alt="" class="wp-image-2723"></figure>



        <p><em><strong>Cordage tendu entre deux points et servant à établir une communication entre deux navires ou entre un navire et la côte, en particulier pour des opérations de sauvetage. Vous trouverez en fin de cet article les sauvetages ou cette technique a été utilisée .</strong></em></p>



        <p>Dès&nbsp;<strong>1791</strong>&nbsp;M&nbsp;<a href="https://fr.wikipedia.org/wiki/Jacques_Joseph_Ducarne_de_Blangy" target="_blank" rel="noreferrer noopener">Ducarne de Blangy&nbsp;</a>imagine un moyen de venir au secours des naufragés en lançant un cordeau depuis la terre ou un autre bâtiment vers le bateau en perdition. En 1801, à plus de 70 ans il&nbsp; publia une brochure&nbsp; avec ce titre:</p>



        <p><em>A la Marine française, ou moyen propre à sauver les équipages d’une partie des vaisseaux qui viennent échouer et périr à la côte par les naufrages.</em></p>



        <p>Dans les années&nbsp;<strong>1860</strong>, s’inspirant des travaux de M De Blangy,&nbsp; M&nbsp;<a rel="noreferrer noopener" href="https://fr.wikipedia.org/wiki/Henri-Gustave_Delvigne" target="_blank">Henri Gustave Delvigne&nbsp;</a>met au point la flèche porte-amarre qui lancée par un&nbsp;fusil&nbsp;ou un&nbsp;canon type perrier&nbsp;permet depuis la terre d’établir une liaison avec un navire en perdition.</p>



        <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Fleche-porte-amarre.png" alt="Flèche porte-amarre" class="wp-image-2195" width="414" height="268"><figcaption>Flèche porte-amarre</figcaption></figure></div>



        <p><strong>1866&nbsp;</strong>&nbsp;la&nbsp;<a href="https://sauveteurdudunkerquois.fr/historique/la-scsn/" data-type="page" data-id="2035">SCSN</a>&nbsp;préconise l’utilisation de l’invention&nbsp; de monsieur Delvigne pour équiper les postes de porte-amarre qu’elle répartie sur tout le littoral.</p>



        <p>Sur le Dunkerquois on en trouve deux sur le secteur de Dunkerque et une sur Fort-Philippe rattachée à la station de Gravelines.</p>



        <p>Ces stations sont généralement du ressort des personnels des Douanes.</p>



        <p><strong>1867&nbsp;</strong>&nbsp;la&nbsp;SCSN&nbsp; donne des consignes très détaillées pour la mise en œuvre de cet équipement.</p>



        <h5>Principes généraux</h5>



        <p>Lorsqu’une première communication a été établie avec un navire naufragé, au moyen de la flèche porte-amarre et de la ligne qu’elle entraîne, il reste, pour établir le va-et-vient, à effectuer quatre opérations.</p>



        <p>1° Au moyen de la ligne, l’équipage naufragé amène le hale-à-bord, et avec le hale-à-bord le&nbsp;<a href="">cartahu&nbsp;</a>double sans fin qui est amarré dessus à terre .</p>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="347" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Va-et-vient-1-1024x347.jpg" alt="" class="wp-image-2724"></figure>



        <p>2° La poulie de ce cartahu est amarrée dans la mâture, ou, si la mâture est tombée, dans la partie la plus élevée du navire .</p>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="373" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Va-et-vient-2-1024x373.jpg" alt="" class="wp-image-2725"></figure>



        <p>3° Les riverains se servent de ce cartahu pour envoyer à bord le bout d’une aussière, que l’équipage amarre à 50 centimètres au-dessus de la poulie du cartahu .</p>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="367" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Va-et-vient-3-1024x367.jpg" alt="" class="wp-image-2726"></figure>



        <p>4° Au moyen de l’aussière raidie à terre et du cartahu sans fin, les riverains font passer à bord une bouée circulaire, garnie de culottes en toile, destinée à recevoir les naufragés un à un et à les porter sur le rivage.</p>



        <figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="346" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Va-et-vient-4-1024x346.jpg" alt="" class="wp-image-2727"></figure>



        <p>5° Lorsque la côte est très-plate et le navire échoué à grande distance , l’aussière ne pouvant plus être employée utilement, on amarre directement la bouée sur le cartahu double et les naufragés sont amenés flottants. On agit de la même manière lorsque la situation du navire naufragé ne laisse pas le temps d’établir l’aussière.</p>



        <h5>Mise en oeuvre du matériel</h5>



        <p>L’appareil va-et-vient est toujours chargé sur le chariot dans son abri et recouvert du prélart en toile ; le trépied en dessous du chariot reposant sur l’essieu, la tête du côté de l’arrière saisie à la barre arrière du chariot au moyen d’une aiguillette faisant tour mort autour des trois montants et passant dans la boucle de la tête. Aussitôt que l’ordre est donné de le diriger sur le lieu d’un naufrage, les hommes qui doivent le manœuvrer se réunissent à l’abri, sous la direction du chef du va-et-vient. Dans chaque poste, cet agent est désigné une fois pour toutes, et chargé spécialement de la surveillance de l’appareil et de l’abri qui le renferme. Le chariot est tiré hors de l’abri et découvert. Les servants chargés de transporter le porte-amarre et ses accessoires prennent ces objets et se dirigent en toute hâte vers le lieu du sinistre. Les autres recouvrent le chariot après avoir allumé le fanal à main, si c’est la nuit.</p>



        <p>Le chef et l’un des servants prennent le timon et relèvent les chambrières. Les autres servants poussent le chariot et l’on se met en marche.</p>



        <p>Le chariot est arrêté en arrière de la batterie; les chambrières sont amenées, le timon et les portes avant et arrière démontés, tout le matériel déchargé et posé à terre. On dispose en premier lieu le cartahu double en plaçant les deux glennes sur le rivage, à 10 ou 15 mètres l’une de l’autre, la poulie au milieu.</p>



        <p>L’aussière est ensuite déposée en arrière du cartahu double ; le trépied établi et la poulie coupée crochée dans son anneau. A défaut d’autre point fixe, on enfonce le pieu dans le sol en ayant soin de placer en avant le madrier si le terrain est mou.</p>



        <p>Dès que la communication a été établie et que l’on voit les naufragés haler sur la ligne, le chef, aidé des numéros impairs, amarre le bout d’en dessous du hale-à-bord sur les deux doubles du cartahu double, à 3 mètres en arrière de la poulie. Il y amarre également la planchette portant l’instruction relative au cartahu double :</p>



        <p><em>Fouettez la poulie le plus haut possible sur le bas mât, ou à l’endroit le plus favorable si les bas mâts sont tombés ; faites un dormant solide. Détachez la ligne ; voyez que la corde court facilement dans la poulie et faites un signal.</em></p>



        <p>Il veille ensuite à ce que le cartahu file à la demande du hale-à-bord, sans faire de coques ni prendre de tours, en mettant à chacun des doubles des hommes qui s’écartent le plus possible en filant.</p>



        <p>Lorsque l’équipage naufragé a halé le cartahu double et fouetté la poulie, il fait un signal en agitant un fanal si c’est la nuit; un pavillon, un chapeau ou tout autre objet, si c’est le jour.</p>



        <p>On amarre l’aussière sur le cartahu double en laissant pendre un bout d’environ o mètres. On y amarre également la seconde planchette d’avis, portant ces mots :</p>



        <p><em>&nbsp;Amarrez cette aussière à deux pieds environ au-dessus de la poulie, faites un dormant solide. Voyez que rien n’engage et que la corde court facilement dans la poulie, puis faites un signal au rivage.</em></p>



        <p>On hale ensuite sur le cartahu double en faisant filer l’aussière à la demande, et en tenant toujours les bouts écartés.</p>



        <p>Pendant ce temps, d’autres servants passent le bout d’en dessous de l’aussière dans la moque à rouet à laquelle a été préalablement aiguilletée la bouée à culotte. Dès que l’équipage naufragé a amarré l’aussière solidement à bord, il fait le signal convenu.</p>



        <p>A ce signal, l’aussière est capelée dans la poulie coupée du chevalet et raidie, au moyen du palan. Le cartahu double est amarré à la bouée au moyen de deux demi-clefs à capeler serrées sur l’aiguilletage. Les servants se portent sur le courant du cartahu double, et liaient dessus pour envoyer la bouée à bord du navire naufragé. Deux hommes filent à la demande l’autre double du cartahu. Dès que la bouée est arrivée à bord et qu’un homme s’est placé dedans, l’équipage naufragé fait un signal.</p>



        <p>A ce signal, les servants se portent sur l’autre courant du cartahu double. Deux d’entre eux restent pour filer à la demande. Cette opération se renouvelle jusqu’à ce que tous les naufragés soient amenés à terre.</p>



        <p>Pendant tout le temps de l’opération, deux servants revêtus de ceintures de sauvetage garnies de lignes, se tiennent prêts à s’avancer dans les brisants pour faciliter au besoin l’arrivée des naufragés.</p>



        <p>Dès que le sauvetage est terminé, on hale à terre le cartahu double. On attend pour reprendre les autres cordages que le temps permette d’aller à bord du navire naufragé, ou que celui-ci étant brisé entièrement, ils puissent être ramenés à terre en tout ou en partie. Le matériel du va-et-vient et du porte-amarre est replacé sur le chariot et reconduit à l’abri.</p>



        <p>Aussitôt rentré, tout ce qui est corde et ligne doit être immédiatement élongé et séché dehors si le temps le permet, et à l’intérieur dans le cas contraire. Des crochets sont disposés dans l’abri, afin que toutes les cordes et lignes puissent être suspendues en guirlandes et séchées aussi promptement qu’il est possible.</p>



        <p>Lorsqu’elles sont complètement sèches, les lignes sont repelotonnées, et. les cordages arrimés de nouveau dans le chariot, tous les objets y sont replacés.</p>



        <p>Cette opération du lovage des grosses cordes, comme celle du pelotage des lignes, doit être accomplie avec grand soin et en défaisant les tours.</p>



        <h5>Dispositions générales</h5>



        <p>Le personnel destiné à manœuvrer le va-et-vient doit être souvent exercé, et surtout dans les premiers temps. Des personnes de bonne volonté pourront se joindre au personnel des douanes et concourir aux exercices.</p>



        <p>Dans les stations réunissant porte-amarre et canot de sauvetage, le chef du service des douanes, faisant partie du comité local, pourra sans doute trouver des auxiliaires dans l’équipage; en tout cas, le chef de pièce et ses servants seront pris parmi les agents des douanes.</p>



        <p>Les manœuvres et exercices du porte-amarre et du va-et-vient se feront toujours sous la direction du chef du service des douanes dans la localité.</p>



        <p>Le chef de pièce est spécialement chargé de l’entretien de la pièce et.de ses accessoires, lignes, hale-à-bord,. etc. Le chef du va-et-vient doit veiller à tout ce qui concerne cet appareil.</p>



        <p>L’un et l’autre surveillent les objets qui leur sont confiés, et doivent réclamer, immédiatement après chaque manœuvre, les réparations et remplacements nécessaires pour que tout soit toujours en état.</p>



