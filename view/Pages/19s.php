        <h1 class="has-text-align-center"><a href="https://www.sauveteurdudunkerquois.fr/Sorties-en-mer-de-1890-%C3%A0-1899/"><br></a>Le sauvetage maritime au 19e siècle</h1>



        <p><em>En ce début de siècle marqué par les guerres napoléoniennes le sauvetage en mer reste confidentiel. </em></p>



        <p><em>Il faudra les 358 morts du&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/10QUE-0213B/">QUEEN ELISABETH&nbsp;</a>pour qu’une réflexion soit entamée par deux personnages clés&nbsp;<strong><a href="http://MOR-2074H/" data-type="URL" data-id="MOR-2074H/">Benjamin Morel</a></strong>&nbsp;et&nbsp;<strong>Jean Joseph Carlier.</strong></em></p>



        <p><em>Cette réflexion les amène à créer en 1834 la&nbsp;Société Humaine de Dunkerque&nbsp;(SHD) dont la structure humaine et matérielle s’inspire de la&nbsp;RNLI&nbsp;de nos amis britanniques et de la Société Humaine de Boulogne.</em></p>



        <p><em>L’année 1857 est marquée par le naufrage des&nbsp;TROIS SŒURS, le 29 janvier 1857, qui entraîne la perte de trois des emblématiques sauveteurs dunkerquois. Cet épisode funeste est sacralisé par la plaque de marbre apposée sur le Leughenar.</em></p>



        <p><em>En 1866, Napoléon III autorise la création de la Société Centrale de Sauvetage des Naufragés (SCNS) dont le champ géographique inclut la totalité des régions côtières de France. La station de Gravelines sera la première station créée sur le dunkerquois. Son patron Joseph <a href="https://sauveteurdudunkerquois.fr/lep-1831h/">Leprêtre </a>est le premier décoré de la légion d’honneur sous l’égide de la SCNS.</em></p>



        <p><em>En 1872 c&rsquo;est François Tixier, le sauveteur emblématique de Dunkerque, qui perd la vie en voulant sauver des naufragés.</em></p>



        <p><em>En 1874 la SHD cesse d’exister et met ses moyens humains et financiers au service de la SCSN qui lui affecte son premier canot, celui de la SHD arrivant en fin de vie.</em></p>



        <p><em>La fin du siècle est marquée par la motorisation des bateaux notamment par la vapeur, et l’arrivée des premiers sauvetages d’aérostiers marquant les prémices des évolutions du 20ème siècle.</em></p>


