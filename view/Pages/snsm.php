        <h1 class="has-text-align-center"><strong>La Société Nationale de Sauvetage en Mer&nbsp; SNSM</strong></h1>



        <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="176" height="176" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SNSM.png" alt="" class="wp-image-820" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SNSM.png 176w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SNSM-150x150.png 150w" sizes="(max-width: 176px) 100vw, 176px"></figure></div>



        <p>À la suite de la Seconde Guerre mondiale, tout est à reconstruire ; la plupart des abris et des moyens nautiques de la  Société Centrale de Sauvetage des Naufragés  et de la Société des Hospitaliers Sauveteurs Bretons.</p>



        <p>La Société Centrale de Sauvetage des Naufragés (SCSN) et la Société des Hospitaliers Sauveteurs Bretons (SHSB) ont la même raison d’être : secourir bénévolement et gratuitement les vies humaines en danger en mer.</p>



        <p>Les deux sociétés ont de plus en plus de mal à remplir pleinement leur mission face au développement du trafic maritime et des loisirs nautiques. Leurs activités sont redondantes.</p>



        <p>Afin d’améliorer l’efficacité au profit du public, une fusion des moyens, des sociétaires et des subsides devient évidente.</p>



        <p>A la demande des pouvoirs publics, la SCSN et les HSB fusionnent en 1967 pour donner naissance à la&nbsp;Société&nbsp;Nationale de&nbsp;Sauvetage en&nbsp;Mer.</p>



