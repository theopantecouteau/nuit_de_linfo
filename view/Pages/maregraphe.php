        <p>Les employés du marégraphe étaient, de par leur positon dans le port, au premier plan pour signaler et agir sur d&rsquo;éventuelles fortunes de mer.  <br>Ainsi <strong>Georges Marquette </strong>participera depuis la terre, le 4 août 1882 au sauvetage de la goélette <a href="https://sauveteurdudunkerquois.fr/82pho-0200b/">PHOEMIE DE GOOLE</a> et le 28 octobre 1892 au sauvetage du <a href="https://sauveteurdudunkerquois.fr/92vis-0259b/">VISCOUNT CASTLEREAGH</a>. <br>Il sera également acteur de sauvetages individuels sur des enfants  en <a href="https://sauveteurdudunkerquois.fr/sauvind188/">1886</a> et <a href="https://sauveteurdudunkerquois.fr/sauvind1906/">1906.</a></p>



        <p><strong>Qu&rsquo;est-ce qu&rsquo;un marégraphe </strong>?</p>



        <p><em><strong>Antoine Marie Rémi Chazallon </strong>1802 &#8211; 1872 invente le  marégraphe en 1844. </em><br><em>Le premier d&rsquo;entre eux est installé à Toulon. </em><br><em>Il est également l&rsquo;éditeur de 1838 à 1862 de l&rsquo;Annuaire des marées des côtes de France.</em></p>



