        <h1 class="has-text-align-center">Le marégraphe de Dunkerque</h1>



        <p>On peut considérer ce poste dépendant de l&rsquo;administration des Ponts-et-Chaussées comme étant l&rsquo;ancêtre de notre météo marine actuelle.</p>



        <p>Les données et documents de cette page sont datés de 1907et signés par l&rsquo;Ingénieur en chef Dreyfus.</p>



        <p></p>



        <p><strong>Les fonctions de l&rsquo;observateur du marégraphe sont les suivantes</strong> :</p>



        <ul><li>Entretien courant des appareils, des piles, du poste etc.</li></ul>



        <ul><li>9h00, 12h00, 15h00, 18h00,  observations journalières météorologiques.</li></ul>



        <ul><li>9h00 relevé, s’il y a lieu, de la quantité d’eau tombée depuis la veille à la même heure.</li></ul>



        <ul><li>12h00 remontage de l’appareil d’horlogerie, renouvellement ou déplacement de la feuille de l’anémomètre, inscriptions journalières sur le registre minute des observations de la journée.<br>Calculs des hauteurs d’eau des marées, moyenne, etc.<br><br>Toutes les heures : relevé des températures maxima et minima, des directions et de la force du vent. </li></ul>



        <p>Toutes ces données doivent être consignées dans des documents prévus à cet effet (Voir bas de page).</p>



