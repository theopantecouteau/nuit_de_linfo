        <h1 class="has-text-align-center">Les Ponts-et-Chaussées</h1>



        <h2 class="has-text-align-center"><a href="https://sauveteurdudunkerquois.fr/phares-et-balises/" target="_blank" rel="noreferrer noopener">Phares et bateaux feux</a> &#8211; Ecluses &#8211; <a href="https://sauveteurdudunkerquois.fr/maregraphe/">Marégraphe</a></h2>



        <p>En 1716, le corps des Ponts et-Chaussées, qui vient d’être créé, est principalement chargé de la construction et de l’entretien des routes, ce qui constitue à l’époque une relative nouveauté, dans la mesure où le réseau routier est embryonnaire et en très mauvais état.</p>



        <p>Au  XIXe et au XXe siècles, cette administration va également prendre en charge les voies navigables, les écluses, les phares, les bateaux-feux et des installations liées au fonctionnement portuaire comme le marégraphe. En 1806 c&rsquo;est la création des Phares et Balises rattachés aux Ponts-et-Chaussées</p>



        <p>Cette administration aura sous sa tutelle les éclusiers, haleurs, équipages des bateaux-feux, personnels des phares et du marégraphe qui, à des degrés divers, seront aussi acteurs des sauvetages en mer et de nombreux sauvetages individuels.</p>



        <p>Deux de ces personnages,<a href="https://sauveteurdudunkerquois.fr/loo-1893h/"> Joseph Looten</a> et <a href="https://sauveteurdudunkerquois.fr/den-4780h/">Frédéric Denquin</a>, &nbsp;seront d’ailleurs élevés à la dignité de Chevalier de la Légion d’Honneur au titre des sauvetages individuels réalisés aux écluses.</p>



        <p>L’écluse Guillain, détruite en 1940, porte le nom d’un ingénieur des Ponts-et-Chaussées qui a réalisé de grands travaux sur Dunkerque entre 1868 et 1881. Ce sera la première écluse à rouvrir en 1947.Elle fut détruite définitivement plus tard.<br>.</p>



        <p>Un buste de cet ingénieur est visible sur le port de Dunkerque.</p>



        <p>Aujourd&rsquo;hui les attributions des Ponts-et-Chaussées sont gérées par diverses structures comme les voies navigables de France.</p>



