        <h1 class="has-text-align-center">Fortune de mer</h1>



        <p><strong>Fortune de mer:</strong><br><em style="font-weight: bold;">Accident survenant au cours d’un voyage en mer et dû à des circonstances liées à l’état de la mer, du vent ou suites d&rsquo;une erreur de navigation.</em></p>



        <p>Les fortunes de mer recensées sur cette page ont été collectées en même temps que les premiers travaux sur les Sauveteurs du Dunkerquois. </p>



        <p>Devant le manque de temps, ce dossier n&rsquo;évoluera plus et restera en l&rsquo;état.</p>



        <p>Le futur moteur intégrera la recherche de patronymes sur  cette partie du site.</p>



        <p>Ces documents, qui dans un premier temps ont été diffusés sur le site<a href="https://www.ghdk-flandre.fr/index.php"> GHDK</a>, sont présentés au format PDF (par période de 25&nbsp; ou 10 ans) et&nbsp; s&rsquo;ouvrent dans une nouvelle fenêtre qu&rsquo;il vous suffit de fermer pour revenir sur cette page.</p>



        <p>Ces documents n&rsquo;ont pas été retravaillés</p>



