        <h1 class="has-text-align-center"><strong>La ligne Torrès</strong></h1>



        <p>&nbsp;L’appareil imaginé par M. Torrès se compose, ainsi que l’indique le croquis ci-dessous, d’une corde de 5 à 7 mètres garnie sur toute sa longueur de cabillots de bois et terminée à l’une des extrémités par une bouée en liège et par un œil.</p>



        <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="718" height="348" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Ligne-Torres.jpg" alt="" class="wp-image-2131" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Ligne-Torres.jpg 718w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Ligne-Torres-300x145.jpg 300w" sizes="(max-width: 718px) 100vw, 718px"></figure></div>



        <p>​</p>



        <p>Lorsque quelqu’un tombe à l’eau le long d’un quai, il peut se faire qu’aucune amarre, aucun objet flottant ne se trouve à portée, et si le malheureux ne sait pas nager, il a le temps de se noyer pendant que l’on court chercher du secours.</p>



        <p>Il arrive parfois, dans les ports de la Manche et de l’Océan, que de braves douaniers, seuls témoins d’accidents de cette espèce et n’ayant rien sous la main, retirent leur capote pour s’en servir en guise de corde.</p>



        <p>Mais parfois aussi l’homme est trop éloigné du bord, ou la hauteur du quai est trop grande, et ce moyen devient insuffisant.</p>



        <p>C’est pour parer à cette absence de moyens de secours que M. Torrès, habitant du Havre, a imaginé l’appareil en question, mais il peut être également utile dans les ports fluviaux et à bord des navires, en servant à amarrer et à soutenir des hommes qui se jettent à l’eau pour en secourir d’autres.</p>



