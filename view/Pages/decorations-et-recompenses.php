        <h2>Prix Henri Durand&nbsp; (De Blois) remis par l&rsquo;Académie Française</h2>



        <div class="wp-block-columns">
            <div class="wp-block-column" style="flex-basis:20%">
                <p></p>



                <figure class="wp-block-image size-full"><img loading="lazy" width="200" height="208" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Medaille-Henri-Durand.png" alt="Médaille Henri Durand de Blois" class="wp-image-1643"/></figure>
            </div>



            <div class="wp-block-column" style="flex-basis:66.66%">
                <p>M. HENRI DURAND (de Blois), ancien négociant, demeurant de son vivant&nbsp;à&nbsp;Paris, et décédé à Nice, le&nbsp;10 mars 1890, sans laisser d&rsquo;héritier à réserve, a institué pour sa légataire universelle la caisse des invalides de la marine, en vertu&nbsp;d&rsquo;un&nbsp;testament en date du 15 septembre 1889, qui contient, à l&rsquo;article 13, au sujet de l&#8217;emploi de sa fortune, des dispositions que nous ayons déjà rappelées lors des premières attributions de ces prix.</p>



                <p>En exécution de ces dispositions testamentaires, à formuler de nouvelles propositions, le comité des inspecteurs généraux de la marine, attribue des prix et des portions de prix, aux personnes méritantes dans la limite de 4 000 francs annuels</p>



                <p><strong>Depuis sa création plus 23 sauveteurs du dunkerquois ont été récompensés</strong>. </p>
            </div>
