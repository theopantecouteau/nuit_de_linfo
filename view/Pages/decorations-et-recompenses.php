        <h2>Prix Montyon appelé Prix de Vertu remis par l&rsquo;Académie Française</h2>



        <div class="wp-block-columns">
            <div class="wp-block-column" style="flex-basis:20%">
                <p></p>



                <figure class="wp-block-image size-full is-resized is-style-default"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Medaille-Monthyon.png" alt="Médaille Prix Monthyon Prix de vertu Académie Francaise" class="wp-image-1548" width="173" height="172"/></figure>
            </div>



            <div class="wp-block-column" style="flex-basis:66.66%">
                <p>Le prix Montyon est un ensemble de prix créés à l&rsquo;initiative de Jean-Baptiste Auget de Montyon et décernés par l&rsquo;Académie Française et par l&rsquo;Académie des Sciences.</p>



                <p>Jean-Baptiste de Montyon avait fondé trois prix, tous trois appelés prix Montyon. Les deux premiers sont décernés par l&rsquo;Académie Française : le premier sous la dénomination de Prix de Vertu, était remis à des personnes méritantes, le second, prix pour l&rsquo;Ouvrage littéraire le plus utile aux mœurs, fut remis pour la première fois en 1782. Le troisième est un prix scientifique remis par l&rsquo;Académie des Sciences.</p>



                <p>Le Prix de Vertu a été maintes fois mentionné par Balzac dans ses romans et vivement critiqué par divers auteurs tels que Rémy de Gourmont ou Charles Baudelaire en raison de l&rsquo;aspect ostentatoire de la charité qu&rsquo;il récompense. Baudelaire a également désapprouvé les prix attribués à ceux qui font une littérature à base de bons sentiments. Il est également critiqué par Octave Mirbeau.</p>
            </div>
        </div>



