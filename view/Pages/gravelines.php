        <h1 class="has-text-align-center"><em>GRAVELINES-</em></h1>



        <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armes-de-Gravelines.png" alt="" class="wp-image-2580" width="137" height="218" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armes-de-Gravelines.png 400w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armes-de-Gravelines-189x300.png 189w" sizes="(max-width: 137px) 100vw, 137px"><figcaption>Armoiries de Gravelines</figcaption></figure></div>



        <blockquote class="wp-block-quote"><p><em>5ème station à rejoindre la Société Centrale de Sauvetage des Naufragés.</em></p><p>Son premier patron,&nbsp;&nbsp;<a href="?controller=site&action=redirect&page=">Joseph Leprêtre</a>,&nbsp;&nbsp;ayant déjà de nombreux sauvetages à son actif</p><p>avant l’installation du canot sera le premier décoré de la Légion d’Honneur sous l’égide de la SCSN</p></blockquote>



        <p>La vigoureuse population de pêcheurs des Forts Philippe accepta avec reconnaissance, l’offre qui lui fut faite de lui confier un canot de sauvetage. Le 19 janvier 1866, elle était mise en possession d’une embarcation et elle a su en tirer un excellent parti dans de nombreuses circonstances très dangereuses.</p>



        <p>La maison-abri a été construite à Grand-Fort-Philippe, sur un terrain domanial à quelques pas de la laisse des hautes mers et à une trentaine de mètres du chenal. Une rampe d’accès provisoire facilite la mise à l’eau de ce côté. Dans les travaux qui seront entrepris à l’Ouest pour continuer le perret jusqu’à la naissance de la jetée intérieure, une cale à pente douce sera réservée pour le service de l’embarcation. Les manœuvres de mise à l’eau et de halage à terre se feront alors avec la plus grande facilité. Il est regrettable que pendant que les travaux de l’Est étaient en cours d’exécution, on n’ait pas pris des précautions analogues pour assurer le service du canot. Jusqu’à ces derniers temps, sa sortie de ce côté était facilitée par un passage qui se trouvait à mi-chemin de la digue. Ce passage, après avoir été maintenu ouvert pendant les travaux de la nouvelle jetée pour l’écoulement des eaux, a été bouché récemment. Il offrait, il est vrai, quelques inconvénients. A la marée ascendante et descendante, il s’y introduisait des courants prenant le chenal en travers et y apportant une certaine gêne à la navigation, mais maintenant, en cas de grands vents du nord, il est excessivement difficile pour le canot de sauvetage de prendre la mer par ses propres moyens pour aller au secours des navires naufragés dans l’Est.</p>



        <p>La station de sauvetage de Gravelines est complétée par un porte amarres à grande portée et un va-et-vient. Ce matériel, confié à la brigade de douanes, est remisé à Petit Fort-Philippe dans un magasin appartenant à l’administration. Pour qu’il soit plus à portée lors d’un sinistre et plus à même, d’être bien entretenu, le comité d’administration a décidé dernièrement de faire construire près, du corps-de-garde, un petit abri qui ne contiendra que les engins de sauvetage.</p>



        <p>Au début la station de Gravelines était conçue comme une société locale en gestion autonome avec l’appui de la SCSN. Ce modèle ne perdura pas et rapidement Gravelines devint une des entités à part entière de la SCSN.</p>



        <p>Depuis 1871, un remorqueur stationne à Dunkerque où il trouve à s’employer et vient, quand il est appelé, entrer les navires à Gravelines. Ils restent ainsi moins longtemps exposés aux dangers du mouillage.</p>



        <p>En 1940 pendant la bataille de Dunkerque l’abri sera endommagé ce qui n’empêchera pas le patron <a href="?controller=site&action=redirect&page=">Brunet</a> aux commandes du <a href="?controller=site&action=redirect&page=">MAURICE PINEL</a> de récupérer des soldats naufragés le 4 juin 1940</p>


