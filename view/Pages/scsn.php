        <h4>La Société Centrale de Sauvetage des Naufragés</h4>



        <figure class="wp-block-image size-full"><img src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Plaque-SCSN-1.png" alt="Plaque de la SCSN décrochée de l'Amiral Ronarc'h lors du passage en SNSM" class="wp-image-2036"><figcaption><strong>Plaque de la SCSN décrochée de l’Amiral Ronarc’h lors du passage en SNSM</strong></figcaption></figure>



        <p>En 1824, au moment où l’œuvre du sauvetage se fondait de l’autre côté du détroit, des efforts étaient faits en France dans le même but.</p>



        <p>Boulogne,&nbsp;<a href="?controller=site&action=redirect&page=" data-type="page" data-id="2026">Dunkerque</a>, Calais voyaient se constituer des sociétés humaines qui ont rendu des services et existent encore aujourd’hui.</p>



        <p>Toutefois, les faibles ressources mises à leur disposition ne leur ont pas permis d’étendre leur action ni d’entretenir dans tous les temps un matériel et un personnel suffisant.</p>



        <p>L’exemple donné par nos villes du nord ne se propagea pas ; des tentatives faites à diverses époques pour constituer un centre d’action furent infructueuses, et nos côtes demeurèrent privées de moyens de secours.</p>



        <p>Frappé des conséquences d’une telle situation, le Ministre des Travaux Publics provoqua, en 1861, la formation d’une commission mixte ayant pour mission de rechercher les mesures les plus propres à assurer, sur le littoral un service complet de sauvetage ; la création de la Société centrale peut être considérée comme le premier corollaire des travaux de cette commission et des mesures dont elle avait proposé l’adoption La société anglaise des Life-boats servira, entre autres, de modèle pour créer la Société Centrale de Sauvetage des naufragés, SCSN.</p>



        <p>L’Amiral Charles Rigault de Genouilly lancera son&nbsp; appel :</p>



        <blockquote class="wp-block-quote"><p><em>Des hommes, des femmes et aussi des enfants ont agi, parfois&nbsp; au péril de leur vie pour sauver leurs semblables que ce soit en mer, dans les ports, les plages, les canaux voire dans les zones habitées. Vous découvrirez la vie et les actes de nos sauveteurs emblématiques&nbsp; (Looten, Lauwick …) et des&nbsp;actes individuels&nbsp;qui méritent d’être rappelés à la mémoire collective.</em></p><p><em>S’ils étaient souvent marins ou proches du monde maritime il ne faut pas oublier nos douaniers qui, à partir des années 1860 assureront, de par leur présence permanente dans le secteur portuaire, le sauvetage d’un nombre conséquent de personnes.</em></p></blockquote>



        <p>La première station du dunkerquois à faire partie de la SCSN sera Gravelines suivie par Dunkerque puis Fort-Mardyck et Malo-les-Bains.&nbsp;</p>



        <p>LA DEUXIEME GUERRE MONDIALE 1939-1945&nbsp;</p>



        <p><br>Pour la troisième fois depuis sa fondation, la Société Centrale va voir son activité brutalement interrompue par une guerre. Mais, alors qu’en 1870 et en 1914 elle n’avait subi d’autre mal que l’arrêt de ses constructions neuves et Ia désorganisation de ses comités et équipages rappelés par la mobilisation, elle devait, cette fois-ci, éprouver de considérables dommages</p>



        <p>Pendant la drôle de guerre ses canots sortent partout, montrant une magnifique activité, avec des équipages de volontaires la plupart du temps. Dans les 52 sorties que l’on relève au cours de cette période, nous citerons seulement les beaux sauvetages de vingt-quatre hommes du dragueur de mines Notre-Dame-des-Dunes par le canot de Gravelines, le 16 janvier 1940, et, le 26 du même mois, celui du chalutier Abel-Allair par le canot de Doelan qui reste près de 32 heures à la mer.&nbsp;</p>



        <p><br>Le 22 mai 1940, notre canot de Gravelines sauve 30 hommes d’un cargo en flammes. Le 5 juin, Gravelines — toujours le Maurice-Pinel, le canot actuel, avec son ancien patron <a href="?controller=site&action=redirect&page=">Edouard Brunet</a> — sauve 6 soldats Français dont 2 gravement blesses. </p>



        <p>L’Amiral Lacaze entreprend alors une démarche auprès de l’Amirauté allemande pour que soit respectée la liberté d’action de nos canots en cas de sinistre en mer. II est écouté&nbsp;avec déférence et compréhension et reçoit des promesses qui ne seront pas toujours tenues. </p>



        <p>Dans certains ports les autorités occupantes maintiennent les réquisitions et les interdictions de sortir en mer… Dans d’autres, nos canots parviennent à faire du bon travail et on peut compter 64 sorties de sauvetage du 1″ juillet 1940 à la Libération mais ils doivent se conformer à des consignes strictes.&nbsp;</p>



        <p>En 1960 le Président de&nbsp; la République fait, sur ses deniers personnels, un don à la SCSN*</p>



