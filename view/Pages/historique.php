        <h1 class="has-text-align-center"><strong>Histoire du sauvetage en mer&nbsp;</strong></h1>



        <h4>Le sauvetage maritime des origines à nos jours</h4>



        <blockquote class="wp-block-quote"><p><strong><sup>Textes et recherches conjointes du webmaster et Lysiane Varlet</sup></strong></p></blockquote>



        <p>On les admire ces grands navigateurs&nbsp; qui, bravant les éléments , sont partis à la conquête des océans. Mais qui dit océan , dit aussi tempêtes, naufrages .&nbsp; Et le naufrage est un élément que l’on retrouve dans la littérature où l’on met en scène des héros,&nbsp;<strong>Ulysse, Sindbad, Virginie, Robinson</strong>… ou des événements, celui de la Blanche-Nef sur le Rocher de Quilleboeuf près de Barfleur dans lequel va disparaître, avec le prince anglo-normand,&nbsp; toute la jeunesse de la cour anglaise…Les catastrophes maritimes s’enchaînent, naufrages, voire massacres, lorsque les malheureux parviennent parfois à la côte, frappant l’imagination.</p>



        <p>Qu’en est-il des tentatives de sauvetages ?</p>



        <p>Lorsqu’un malheureux tombait à la mer, il fallait s’en apercevoir et pouvoir s’arrêter pour le récupérer, avec la vapeur, le bruit de la machine et la rapidité de la marche, comment entendre les cris de détresse….</p>



        <p>Aucune obligation réglementée, aucune association pour porter assistance ; au contraire, dans de nombreuses régions, et jusqu’à une époque relativement récente le naufrage était une aubaine et il arrivait même qu’il soit provoqué à l’aide de feux allumés en haut des falaises…</p>



        <p>Quelques royaumes avaient cependant commencé à réagir.</p>



        <p>Aliénor d’Aquitaine qui fut d’abord reine de France puis reine d’Angleterre s’intéresse au droit maritime et promulgue les Rôles et Jugements en&nbsp;<strong>1160</strong>&nbsp;qui serviront de base à notre droit maritime. Ainsi seront protégés les biens de l’équipage, la cargaison mais aussi les vies humaines afin que cessent les actes de barbarie des populations qui massacraient les naufragés.</p>



        <p>Le Danemark paraissait au commencement du XIV siècle bien préparé&nbsp;&nbsp; aux découvertes maritimes et la reine Marguerite, surnommée la Sémiramis du Nord avait réussi à humilier la Ligue hanséatique en donnant à ses établissements maritimes un &nbsp;caractère de noblesse et de justice&nbsp; associé à celui de&nbsp; force.</p>



        <p>Pour cela, dans un édit publié en&nbsp;<strong>1396</strong>,&nbsp; la&nbsp;<strong>Reine Marguerite</strong>&nbsp;ordonne qu’aucun droit ne soit &nbsp;perçu autre que ceux que la loi ordonne , que tout secours soit&nbsp; accordé aux bâtiments étrangers en cas de naufrage ou de détresse sans&nbsp; exiger d’autre récompense que celle que la loi fixe&nbsp;; enfin que «&nbsp;tout ce que l’humanité conseille et qui se trouve&nbsp; en accord avec une saine politique soit&nbsp; mis en usage envers les étrangers .&nbsp;»</p>



        <p><em>La Ligue hanséatique également appelée germanique ou teutonique et l’association historique des villes marchandes de l’Europe du Nord, autour de la mer du Nord et de la mer Baltique, &nbsp;&nbsp;se distinguent en ce&nbsp; sens que leur commerce s’appuie sur des privilèges jalousement défendus qui leur sont octroyés par divers souverains européens. Cette Ligue a été active du 12e au 17e siècle pour disparaître en 1648 suite au&nbsp;&nbsp; traité de Wesphalie,&nbsp; signant la fin de la guerre de 30 ans et de la guerre de 80 ans.</em></p>



        <p>L’important était le sauvetage du navire et des marchandises. Tant mieux si quelques marins réussissaient à être sauvés. On refusait même parfois à envoyer un canot de sauvetage quand cela était possible.</p>



        <p>En&nbsp;<strong>1749,</strong>&nbsp;les échevins du Havre et des représentants des sociétés de commerce créent un magasin de sauvetage avec tout ce qui est nécessaire pour intervenir en cas de naufrage.</p>



        <p>Dans les Flandres,&nbsp; <a href="?controller=site&action=redirect&page=">une ordonnance de&nbsp;<strong>1770</strong>, &nbsp;signée de&nbsp;<strong>Caumarti</strong></a><strong>n</strong>, intendant des Flandres et reprise&nbsp; par les baillis de la région de Dunkerque donne des directives sur la conduite à tenir devant une personne noyée.</p>



        <p>En&nbsp;<strong>1775</strong>, M. de&nbsp;<strong>Bernier</strong>, contrôleur général des Ponts et Chaussées, conçoit une première embarcation de sauvetage….L’idée sera reprise par les Anglais.</p>



        <p>En Angleterre, en&nbsp;<strong>1789</strong>,&nbsp;<strong>Henri Greathead</strong>&nbsp;invente le canot de sauvetage insubmersible. En 1803, 31 canots sont construits par ses chantiers .</p>



        <p>En <strong>1824 </strong> c’est la création de la «&nbsp;Royal&nbsp;Institution for the&nbsp;Preservation of&nbsp;Life from Shipwreck&nbsp;» (Institution Royale pour le Sauvetage de la Vie des Naufrages) : Elle a été financée grâce à un appel à la Nation *, lancé par Sir William Hillary. Elle deviendra plus tard la «&nbsp;Royal&nbsp;National&nbsp;Lifeboat&nbsp;Institution&nbsp;» (Institut National du Bateau de Sauvetage). </p>



        <p>Les Anglais ont déjà une bonne expérience des canots de sauvetages qu’ils perfectionnent depuis 1789, notamment par Henry Greathead, celui que l’on reconnaît comme l’inventeur du canot de sauvetage insubmersible. La principale qualité de ce canot de 9,14 m, large de trois mètres et pointu des deux bouts, est d’être insubmersible, grâce à un doublage intérieur en liège et l’adjonction d’une ceinture de défense également en liège. </p>



        <p>Les Anglais fourniront nombre de Sociétés de Sauvetage Françaises en canots et en plans Anglais. En 1803, 31 canots de ce type ont été construits par les chantiers Greathead.</p>



        <p>La RNLI&nbsp; est toujours en activité.</p>



