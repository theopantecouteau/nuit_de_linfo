                        <p>Né à Hambourg le 10 avril 1800</p>



                        <p>Mort à Toulon le 18 octobre 1887</p>



                        <p>Militaire et inventeur français.</p>



                        <p>Officier d’infanterie dans la Garde Royale.</p>



