        <p>Le principe de la flèche Delvigne consiste à envoyer avec celle–ci vers un bateau en perdition une ligne de vie.</p>



        <p>Cette flèche est envoyée par une arme à feu</p>



        <p>En effet la flèche Delvigne, peut s’adapter aux armes à feu de toute sorte et de toute dimension.</p>



        <p>C’est une baguette en bois ou en fer portant à ses deux extrémités, ou seulement même à son extrémité postérieure, une virole saillante. On fait autour de la baguette, au moyen de plusieurs tours de ligne, un coulant glissant à frottement dur d’un bout à l’autre.</p>



        <p>Sur le coulant on place une bague embrassant et serrant la flèche, et aux deux doubles&nbsp;de cette bague on amarre l’extrémité de la ligne destinée à établir la communication. Après avoir versé dans l’arme à feu de la poudre et placé une bourre par-dessus, on introduit la flèche, la virole la première lorsqu’il n’y en a qu’une; on fait glisser le coulant de manière à ce qu’il se trouve hors du canon, la bague est placée sur le coulant, la ligne forme une pelote ordinaire que l’on pose à terre.</p>



        <p>On en prend le bout intérieur pour l’amarrer à la bague. Lorsque le coup part, la bague et le coulant glissent le long de la flèche et s’arrêtent à la virole. La ligne est mise en mouvement progressivement, et le choc qu’elle reçoit, se trouve en partie amorti.</p>



        <p>​</p>



        <p>C<strong>es matériels équipaient principalement les postes de Douanes.</strong></p>



        <h2><strong>Tir au fusil</strong></h2>



        <p>On utilisera un mousqueton ou un fusil de rempart suivant les cas&nbsp;:</p>



        <p>Le mousqueton, portée de 30 à 40m selon la direction du vent (Flèche Delvigne en bois) approprié pour le lancement d’amarre dans les ports.</p>



        <p><br>Le fusil de rempart, portée de 80 à 90m selon la direction du vent (Flèche Delvigne en bois) approprié pour les sauvetages en bords de&nbsp;côte.</p>



        <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Tir-au-fusil.png" alt="Fusil et flèche Delvigne" class="wp-image-2192" width="751" height="349" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Tir-au-fusil.png 744w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Tir-au-fusil-300x140.png 300w" sizes="(max-width: 751px) 100vw, 751px"><figcaption><strong>Fusil et flèche Delvigne</strong></figcaption></figure></div>



        <h2>T<strong>ir au canon</strong></h2>



        <p>La flèche Delvigne peut également être tirée depuis un bateau avec un canon «&nbsp;Perrier «&nbsp; réformé.</p>



        <p>Pour procéder au tir des flèches porte-amarres au canon il faut commencer par installer le canon de 4, monté&nbsp;sur son affût d’embarcation, dans l’endroit du pont qu’on jugera le plus convenable à ce genre de tir, et de façon&nbsp;à ce qu’aucun cordage ne se trouve sur le trajet de la flèche.</p>



        <p>​</p>



        <p>On placera ensuite le grand coin en bois sous le châssis, et l’on vérifiera avec le quart de cercle si l’angle de tir est de 30 degrés ; le coin de mire étant enlevé au préalable et le canon reposant sur la semelle de l’affût. Si l’on tire vent arrière et par coup de vent, on emploiera un angle de 40 à 45 degrés qui, dans ces circonstances-là, peut être plus avantageux s’il arrive dévider à l’avance quelques mètres de ligne à chacune des pelotes et du bout qui est lové à l’intérieur de la pelote, surtout pour la première pelote, qui est de beaucoup celle qui fatigue le plus dans le tir, par suite de la grande vitesse de la flèche au départ, et qui fournit à elle seule presque toutes les ruptures de ligne.</p>



        <p>​</p>



        <p>Lorsqu’on tire vent de travers, il faut, suivant la force de la brise, tirer au vent du point à atteindre; à 300 mètres de distance et par une jolie brise, la déviation peut aller jusqu’à 50 mètres. Elle est moins forte pour les autres directions du vent et peu sensible pour le vent de bout et le vent arrière ; dans ces deux cas, elle est tantôt à droite, tantôt à gauche de quelques mètres; il faudra donc viser sur le but lui-même. En somme, le tir des porte-amarres est loin d’être aussi mauvais qu’on serait porté à le croire à première vue d’après les éléments qui y sont employés.</p>



        <p>​</p>



        <p>Il faut attendre, pour mettre le feu à la pièce, que le navire soit dans une position moyenne à ses balancements de roulis. S’il est échoué, on devra tenir compte de l’inclinaison du pont pour le pointage, ce qui est toujours très-facile au moyen du quart de cercle.</p>



        <p>​</p>



        <p>Les portées sur lesquelles on peut compter, par calme ou petite brise, avec les flèches porte-amarres, sont de 300 mètres environ avec la ligne de 4,5mm et de 250 mètres avec la ligne de 6.5mm.&nbsp;Elles augmentent, rapidement quand le vent fraîchit et vient de l’arrière, ce qui est le cas qui se présentera le plus souvent pour un navire mis à la côte ; dans un tir simultané, vent de bout et vent arrière, avec flèches en bois, par une brise assez fraîche, sous l’angle de 30 degrés , la portée vent arrière a été d’environ 75 0/0 plus forte que celle vent de bout, c’est-à-dire de 200 mètres contre 355 mètres, et 255 mètres contre 375 mètres.</p>



        <p>​</p>



        <p>Il est probable que dans un coup de vent et vent arrière, on atteindrait, sous l’angle de 40 degrés, des portées de plus de 400 mètres. que le coin de pointage ne suffise pas pour obtenir l’angle qu’on désire, et cela arrivera nécessairement pour l’angle de 40 degrés, il suffira d’exhausser l’avant du châssis en y plaçant quelques bouts de planche. Il n’est pas nécessaire, d’ailleurs, de s’attacher à obtenir exactement l’angle de tir, un degré en plus ou en moins ayant peu d’influence sur les portées. Pour terminer l’installation de la pièce, on fixera les deux extrémités de la brague*&nbsp; après les taquets , boucles ou pitons du pont ou de la muraille du bâtiment qui se trouvent à portée du canon, en réglant la longueur de la brague de telle sorte que l’affût puisse reculer jusque vers l’extrémité du châssis. On placera successivement dans l’âme une gargousse et une flèche garnie, en refoulant légèrement avec celle-ci sur la gargousse pour bien assurer sa position au fond de l’âme, et l’on placera, sous la tige des flèches en fer, à l’entrée du canon, un petit bouchon en étoupe pour que l’axe de la flèche soit à peu près parallèle à celui du canon. Avant d’introduire la flèche dans l’âme, il faut la graisser avec soin sur toute la longueur, au moyen d’une brosse à platine et de saindoux; il faut aussi faire jouer les coulants et les attaches sur la tige, pour s’assurer qu’ils sont convenablement serrés et en état, de bien fonctionner. La pratique seule peut bien enseigner tous ces détails relatifs aux lignes; ils ont une importance capitale, et l’on ne saurait y apporter trop de soin, car ils constituent la partie la plus importante et la plus délicate du système, et si les lignes ne sont pas en parfait état, ainsi que les coulants et les attaches, on cassera les premières presque à tout coup, et l’on n’aura plus dans les mains qu’un instrument inutile.</p>



        <p>​</p>



        <p>Les pelotes de lignes (au nombre de 4 ou 8 suivant le cas), préparées à l’avance, seront placées sur le pont, à un mètre l’une de l’autre s’il est possible, sur une ligne perpendiculaire ou parallèle au plan de tir, suivant les circonstances locales ; la première pelote sera fixée aux deux attaches de la flèche par trois tours morts ou deux tours demi-clefs; chaque pelote sera, en outre, réunie à la suivante au moyen d’un nœud droit bien serré, et la dernière sera enfin amarrée au bâtiment, afin de faciliter le délovage des pelotes.</p>



        <p>​</p>



        <p>Pour éviter soit les ruptures de ligne, soit que des portions de pelotes soient entraînées sans être délovées, il faut dévider à l’avance quelques mètres de ligne à chacune des pelotes et du bout qui est lové à l’intérieur de la pelote, surtout pour la première pelote, qui est de beaucoup celle qui fatigue le plus dans le tir, par suite de la grande vitesse de la flèche au départ, et qui fournit à elle seule presque toutes les ruptures de ligne.</p>



        <p>Lorsqu’on tire vent de travers, il faut, suivant la force de la brise, tirer au vent du point à atteindre; à 300 mètres de distance et par une jolie brise, la déviation peut aller jusqu’à 50 mètres. Elle est moins forte pour les autres directions du vent et peu sensible pour le vent de bout et le vent arrière ; dans ces deux cas, elle est tantôt à droite, tantôt à gauche de quelques mètres; il faudra donc viser sur le but lui-même. En somme, le tir des porte-amarres est loin d’être aussi mauvais qu’on serait porté à le croire à première vue d’après les éléments qui y sont employés.</p>



        <p>Il faut attendre, pour mettre le feu à la pièce, que le navire soit dans une position moyenne à ses balancements de roulis; s’il est échoué, on devra tenir compte de l’inclinaison du pont pour le pointage, ce qui est toujours très-facile au moyen du quart de cercle.</p>



