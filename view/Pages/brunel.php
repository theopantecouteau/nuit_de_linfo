        <h1 class="has-text-align-center"><strong>La ligne BRUNEL</strong></h1>



        <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Technique-Ligne-Brunel.jpg" alt="Ligne BRunel" class="wp-image-2111" width="281" height="211"><figcaption><strong>Ligne Brunel</strong></figcaption></figure></div>



        <blockquote class="wp-block-quote"><p><em>Très utilisée par les douaniers en faction au bord de mer l’utilisation de cette ligne a permis de sauver de la noyade bon nombre de personnes.</em></p></blockquote>



        <p>Au nombre des petits engins de sauvetage utilisés dans plusieurs de nos ports pour porter secours aux personnes qui tombent accidentellement à l’eau, figure la ligne inventée par M.&nbsp;<strong>Brunel,</strong>&nbsp;lieutenant des Douanes, aujourd’hui en retraite à Dieppe. </p>



        <p>Ce petit instrument, aussi simple que pratique, rend les plus grands services et il nous a paru utile d’en donner ci-après la description : La ligne Brunel qui est portative a été inventée en 1874 par M. Brunel, lieutenant des douanes à Dieppe. Dans sa longue carrière, cet officier inventeur de plusieurs autres appareils de sauvetage avait eu l’occasion de constater souvent l’insuffisance des moyens de secours dans les ports. Aussi sa préoccupation constante étant de rechercher ce qu’il y aurait à faire pour combler cette lacune, il imagina ce petit engin très simple et très maniable.</p>



        <p>Indépendamment du grand avantage d’être portative et de pouvoir à la rigueur être mise dans sa poche, la ligne Brunel a celui non moins grand de pouvoir être développée très rapidement sur une longueur double de&nbsp;celle de la plus grande gaffe. Avec l’autorisation de M. le directeur général des douanes, ce petit engin fait désormais partie dans presque tous les ports de l’équipement de chaque factionnaire qui le remet à celui qui vient le relever de quart. Il se place généralement au ceinturon, quelquefois à la poignée du sabre</p>



        <p>Recommandations.&nbsp;: Pour sortir facilement l’appareil de sa gaine il faut rabattre en dessous la grande languette de cuir qui se trouve à l’intérieur et tirer dessus verticalement</p>



        <p>Cet appareil se compose d’une petite ligne très solide enroulée sur un flotteur en bois auquel elle tient d’un bout, l’autre extrémité étant fixée à un grappin à quatre branches dont le corps garni de plomb se place dans l’intérieur du flotteur. Une gaine en cuir sert d’enveloppe à l’engin. Manière de s’en servir</p>



        <p>Quand le douanier aperçoit une personne en danger de se noyer, il peut s’en servir de deux manières</p>



        <p>&nbsp; 1 Si la personne tient bien à l’eau mais, est entrainé pour une cause ou une autre, le douanier garde le grappin en main&nbsp; et lance le bâton qui, faisant flotteur, peut être saisi par l’individu.</p>



        <p>&nbsp; &nbsp;2&nbsp; Si la personne ne peut résister à la mer où&nbsp;est en train de se noyer le douanier garde le bâton et lance le grappin sur la personne pour l’agripper</p>



        <p>Après avoir sorti l’appareil de sa gaine le moyen de s’en servir est des plus simples. Il suffit d’ôter le grappin de l’intérieur du flotteur, dans lequel il est placé, de le mettre dans la main gauche et de lancer avec la main droite la pelote de ligne, en la tenant horizontalement, le bout de la corde en dessous.</p>



        <p>C’est alors que si la personne ne saisissait pas le flotteur, ou la ligne même, on remonte le tout et on envoie aussitôt le grappin en ayant soin quand l’espace le permet, de le jeter à quelques mètres au, delà du but à atteindre. (Fig. 3).</p>



        <p>Avec cette, précaution on a le temps de faire prendre une bonne direction à l’engin qui ne doit pas être conduit trop rapidement. Il est entendu que si la nécessité commandait de se servir immédiatement du grappin, il faudrait dérouler la longueur de ligne jugée nécessaire, ce qui se fait du reste à la minute.</p>



        <p>Le bout du flotteur où se trouve le grappin doit toujours être tourné du côté de la lanière destinée à accrocher l’appareil au ceinturon ou à la poignée du sabre. Ce bout ne doit sortir de la gaine que le dernier ; c’est le contraire qui a lieu pour le mettre en place.</p>



        <p>Quand la ligne a été mouillée à l’eau de mer il est essentiel de la passer à l’eau douce; de la faire bien sécher sans l’approcher du feu à cause du flotteur qui pourrait se fendiller et de l’étirer avant de l’enrouler. Le lovage sur le flotteur doit être bien serré et bien régulier.</p>



        <p>Dans le cas où un des bras du grappin se trouverait forcé par la rencontre d’un objet résistant, il suffit pour lui faire reprendre sa forme primitive de replacer le grappin dans le flotteur et de frapper sur la branche forcée avec un marteau ou même avec un galet de la plage.</p>



        <p>Ne jamais chercher à soulever avec le grappin une personne au delà de la surface de l’eau.</p>



