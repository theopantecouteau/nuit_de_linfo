            <h2 class="has-text-align-left">1879</h2>



            <p>Après lecture du rapport de M. le Lieutenant de Vaisseau <strong>Ragiot</strong> sur sa tournée dans le Nord, le Comité décide la création de la station de sauvetage de Fort-Mardyck et que la construction d&rsquo;une maison-abri sera demandée à M. le Ministre des Travaux Publics.</p>



            <p>Le Comité décide que le canot construit en Allemagne sera expérimenté à Mardyck.</p>



            <p>A Fort-Mardyck, le canot <a href="https://sauveteurdudunkerquois.fr/marie-bouteneff/">MARIE BOUTENEFF</a> est remisé dans un hangar qui a été solidement réparé ; il permettra d&rsquo;attendre le printemps pour l&rsquo;établissement de l&rsquo;abri en bois qui doit recevoir l&#8217;embarcation.</p>



            <p>Au coeur des Salines, sur le territoire de Grande-Synthe, jusqu&rsquo;à la fin de la première guerre mondiale, se dressait le hangar abritant le canot de sauvetage.</p>



            <p>Cette station du canot de sauvetage &nbsp;avait été créée en 1879&nbsp;en raison des bancs de sable dangereux&nbsp;que présente la côte en face de Fort-Mardyck. Cependant, elle ne peut&nbsp; proposer qu&rsquo;un rayon d&rsquo;action très restreint.</p>



            <p>En 1910 le canot&nbsp;<a href="https://sauveteurdudunkerquois.fr/marie-bouteneff/">MARIE DE BOUTENEFF</a>&nbsp;est remplacé par la&nbsp;<a href="https://sauveteurdudunkerquois.fr/sainte-aline/">SAINTE ALINE</a>. Ce dernier, dans l&rsquo;état actuel des connaissances ne fait aucune intervention.</p>



            <p>La terre ne cessant de gagner sur la mer, cette station se trouve en 1912 à 400 ou 500 mètres du rivage ;&nbsp;il faut alors quatre chevaux pour tirer le chariot et conduire le canot jusqu&rsquo;à la plage, lui faire traverser la dune et lui faire parcourir l&rsquo;estran d&rsquo;un kilomètre à marée basse.</p>



            <p>Etant donné qu&rsquo;il serait impossible de lancer le canot par gros temps, que la station ne dispose que de mauvais moyens d&rsquo;information, le village étant masqué de la mer et étant éloigné de deux kilomètres du rivage, enfin que les canots de Dunkerque, aidés des remorqueurs du port rendraient même tardive et inutile l&rsquo;intervention des sauveteurs fort-mardyckois, cette station doit être classée au dernier rang pour les services qu&rsquo;elle est susceptible de rendre.</p>



            <p>Durant la 1ère Guerre Mondiale la maison abri est réquisitionnée pour les besoins du conflit. A la fin de celui-ci l&rsquo;arrivée du canot de sauvetage motorisé EUGENE VOIELLAUD à Dunkerque sonnera le glas de la station.</p>



            <h2>1919</h2>



            <p>La station est supprimée. La&nbsp;<a href="https://sauveteurdudunkerquois.fr/sainte-aline/">SAINTE ALINE</a>, condamnée, est vendue. Et pourtant&nbsp;la station du canot de sauvetage a rendu de fiers services pour venir en aide à des équipages désemparés.</p>



