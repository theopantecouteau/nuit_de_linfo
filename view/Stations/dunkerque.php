        <h1 class="has-text-align-center"><strong>Station de Dunkerque</strong></h1>



                <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armoiries-Dunkerque.png" alt="Armoiries Dunkerque" class="wp-image-2058" width="192" height="211" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armoiries-Dunkerque.png 400w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Armoiries-Dunkerque-273x300.png 273w" sizes="(max-width: 192px) 100vw, 192px" /></figure></div>



                <h5><strong>1834</strong></h5>



                <p>Création de la <a href="https://sauveteurdudunkerquois.fr/naissance-du-sauvetage-dunkerquois/">société humaine,</a> soutenue par la municipalité, la Chambre de Commerce et par le service du pilotage ,  cette nouvelle institution ouvre bientôt deux postes de secours :</p>



                <p>Le premier à l’est du port près de l’Estran au delà de l&rsquo;écluse de la lunette&nbsp; (1)&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</p>



                <p>Le second à l’ouest, près du bassin de chasse. (2)</p>



                <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="356" height="355" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SH-Localisation-des-locaux-en-1834.png" alt="SH Localisation des locaux en 1834" class="wp-image-2059" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SH-Localisation-des-locaux-en-1834.png 356w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SH-Localisation-des-locaux-en-1834-300x300.png 300w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/SH-Localisation-des-locaux-en-1834-150x150.png 150w" sizes="(max-width: 356px) 100vw, 356px" /><figcaption><strong>SH Localisation des locaux en 1834</strong></figcaption></figure></div>



                <p>Idéalement situé, le premier se voit confier l’unique canot de sauvetage alors disponible sur le littoral dunkerquois. Équipée de deux voiles et bordant six avirons, cette embarcation de 9 mètres est également dotée de caissons à air d’insubmersibilité lui permettant de se redresser après un chavirage.</p>



                <h5>1857</h5>



                <p>Au moment ou la Société Humaine va perdre trois de ses sauveteurs dans le naufrage des Trois Sœurs un nouveau canot, de type anglais, vient d&rsquo;arriver pour remplacer le précédent qui n&rsquo;est plus opérationnel. Bloqué par les douanes il ne pourra servir en cette funeste journée.</p>



                <h5><strong>1872</strong>&nbsp;</h5>



                <p>La Société&nbsp; humaine est annexée à&nbsp; la&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/La-SCSN/">Société Centrale de sauvetage des naufragés</a>. Le premier canot de la SCSN, le NOUVEAU DUNKERQUE, est&nbsp;&nbsp;installé dans un abri provisoire en briques et planches, à la naissance de l&rsquo;estacade est , de l&rsquo;autre côté de la route (3). Il est à une vingtaine de mètres de la rampe d&rsquo;accès construite pour le descendre dans le chenal, manœuvre qui avec des hommes exercés se fait en moins de dix minutes. Il doit alors parcourir 1300 mètres pour sortir des passes ce qui est trop long pour porter secours à un navire en détresse où chaque minute compte. Il doit sortir à l’aviron quand la marée est trop basse pour que les remorqueurs puissent lui apporter un soutien.</p>



                <h5><strong>1876</strong>&nbsp;</h5>



                <p>Un nouvel abri est construit 600 mètres plus loin&nbsp; (à proximité des écluses de chasse). L&rsquo;abri donne accès à un plan incliné permettant une mise&nbsp;à l&rsquo;eau aisée du canot.(4)</p>



                <p>La chambre de commerce de Dunkerque a mis à la disposition du Comité de sauvetage ses remorqueurs pour faciliter la sortie du canot quand il aura à se porter au secours d&rsquo;un bâtiment en détresse. Cette décision entérine une situation mise en œuvre depuis 1862 </p>



