                <h5><strong>1878&nbsp;</strong></h5>



                <p>La société humaine équipe 3 remorqueurs de postes de secours avec fusil lance amarre.</p>



                <h5><strong>1880</strong>&nbsp;</h5>



                <p>Un généreux donateur offre à la SCSN un canot de sauvetage&nbsp; pour Dunkerque qui portera le nom de SUSAN GRAY. Ce canot est affecté à la jetée est. La station de Dunkerque est maintenant équipée de deux canots.</p>



                <p>Le deuxième canot sera installé&nbsp;à flot près de l&rsquo;écluse dite de la « belle inutile » située&nbsp; au pied du Leughenaer (recouverte au début du 20e siècle).(5)</p>



                <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Canot-de-sauvetage-et-lamaneur-pres-de-la-belle-inutile.jpg" alt="Canot de sauvetage et lamaneur près de la belle inutile" class="wp-image-2061" width="575" height="366"/><figcaption>Canot de sauvetage et lamaneur près de la belle inutile</figcaption></figure></div>



                <h5><strong>1880</strong></h5>



                <p>La société installe un poste de secours au Risban (6) et le deuxième au lieu&nbsp; dit Cayenne*. Les matériels qui y sont entreposés sont du ressort des brigades des Douanes. Ils sont équipés de&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/Canon/">canons&nbsp;</a>et de&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/Fusil/">fusils&nbsp;</a>destinés à lancer les flèches&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/Delvigne/">Delvigne&nbsp;</a>qui permettront d&rsquo;établir des&nbsp;<a href="https://www.sauveteurdudunkerquois.fr/Va-et-vient/">va-et-vient</a>&nbsp;entre la terre et le bateau en détresse.</p>



                <p>*Le poste de Cayenne est situé, approximativement, dans le secteur de l&rsquo;actuelle station de sauvetage</p>



                <p>​​</p>



                <h5><strong>1913&nbsp; Modernisation</strong></h5>



                <p>D&rsquo;importants travaux  sont entrepris par la Société Centrale de Sauvetage des Naufragés  pour recevoir et utiliser le canot à moteur « COMMANDANT EUGENE VOIELLAUD&nbsp;» . Ils consistent en la construction d&rsquo;une maison-abri prolongée par une glissière en bois de 70 mètres de longueur supportée par des pilotis. Grâce à la collaboration dévouée du service des Ponts-et-Chaussées, rien n&rsquo;est négligé pour donner à cette installation toute la perfection désirable. Nous aurons, donc bientôt, dans notre grand port du Nord, une station de sauvetage modèle et essentiellement efficace : canot puissant et rapide pouvant être lancé tout armé en quelques secondes, canot sur lequel tous pourront compter, car le type en est éprouvé ; les deux similaires « Emile Perrin » et « Sainte Lucie » en service depuis un ou deux ans ne nous ont, en effet, donné aucun mécompte. Nous étions sûrs de l&rsquo;excellence de leurs formes qui sont celles de nos canots inchavirables ; l&rsquo;expérience nous a montré que nous pouvions faire la même confiance à leur mode de propulsion.&nbsp;Le canot arrivera en réalité en 1919 ..</p>



                <div class="wp-block-image"><figure class="aligncenter size-full is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Canot-Eugene-Voiellaud.jpg" alt="Canot Eugene Voiellaud" class="wp-image-2064" width="331" height="498" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Canot-Eugene-Voiellaud.jpg 541w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Canot-Eugene-Voiellaud-199x300.jpg 199w" sizes="(max-width: 331px) 100vw, 331px" /><figcaption>Canot Eugene Voiellaud</figcaption></figure></div>



                <h5><strong>Le 29 Mai 1940</strong></h5>



                <p>A 9 h20 une bombe incendiaire tombait sur la station de sauvetage de Dunkerque. A 10 heures, sans qu&rsquo;aucune intervention ne soit&nbsp;possible il ne reste&nbsp;plus rien de l&rsquo;abri ni du canot.</p>



                <p>En mai 1940 la station de Dunkerque est complètement détruite. Dunkerque reste occupé jusqu&rsquo;au 9 mai 1945. La reconstruction du port est lancé. Le port est de nouveau ouvert en 1946&nbsp; et l&rsquo;écluse Wattier ouvre en 1947.</p>



                <p>En 1948, le 10 décembre, la catastrophe du RAPHAEL face à Mardyck qui fera 6 morts en laissant&nbsp; 14 orphelins mettra en exergue le manque cruel de moyens de sauvetage sur le dunkerquois.</p>



                <p>Dunkerque n&rsquo;a toujours pas de canot et celui de Gravelines est en réparation.</p>



                <p>Ces événements ont sans doute contribué à ce que la SNSM se décide à remettre en place une structure de sauvetage sur Dunkerque</p>



                <h5><strong>1949&nbsp; La reconstruction</strong></h5>



                <p>Ce sera chose faite le&nbsp;23 mars 1949 avec la première réunion du comité de Dunkerque de la SCSN.</p>



                <p><br>Il est demandé, par les instances nationales et les organes portuaires, à Maitre&nbsp;<strong>Jean Allemes&nbsp;</strong>Notaire, d&rsquo;en assurer la présidence.<br>Le vice-président sera le lieutenant de vaisseau de réserve &nbsp;<strong>Paul Raoul&nbsp;</strong>&nbsp;Chef des services du pilotage<br>Le secrétaire trésorier sera&nbsp;<strong>Emmanuel Allemes</strong>&nbsp;&nbsp;courtier maritime et constructeur de navire<br>Membres&nbsp;<br><strong>&nbsp; &nbsp;&#8211; Le Gorgeu</strong>&nbsp;ingénieur en chef des Ponts-et-Chaussées<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Ternant</strong>&nbsp;Ingénieur des Ponts-et-Chaussées<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Blanche</strong>&nbsp;Administrateur de l&rsquo;inscription maritime</p>



                <p>&nbsp; &nbsp;&#8211;&nbsp;<strong>Emile Dubuisson&nbsp;</strong>Président de la &nbsp;&nbsp;chambre de commerce<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Seguin</strong>&nbsp;&nbsp;directeur de la compagnie des bateaux à vapeur du Nord<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Léon Duboc</strong>&nbsp;Commandant du port<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Louis Ayello</strong>&nbsp;Armateur<br>&nbsp; &nbsp;&#8211;&nbsp;<strong>Six &nbsp;</strong>&nbsp;Commandant Inspecteur de la navigation maritime à Dunkerque</p>



                <p><br><em>Le président avoue avoir été surpris d&rsquo;être choisi par les instances pour mettre en place ce premier comité d&rsquo;après guerre.&nbsp;Il compte sur ses collègues pour l&rsquo;épauler de par leurs connaissances maritimes.</em></p>



                <p>Le nouveau canot AMIRAL RONARC&rsquo;H&nbsp;appareillera de Sartouville pour s&rsquo;arrêter à Rouen où son équipage dunkerquois le prendra en charge.<br>Celui-ci&nbsp;le conduira d&rsquo;abord au Havre où le commandant<strong>&nbsp;Durand Gosselin</strong>&nbsp;inspecteur de la SCSN montera à bord pour faire la traversée jusqu&rsquo;à Dunkerque<br>L&rsquo;arrivée est escomptée pour le jeudi 7 avril à la cale des pêcheurs, quai du Leughenaer, avec comme premier équipage&nbsp;<br>Patron du canot &nbsp;<strong>Auguste Brunet</strong>&nbsp;Pilote dunkerquois<br>Sous Patron&nbsp;<strong>Louis Bossu&nbsp;</strong>Patron lamaneur<br>Mécanicien&nbsp;<strong>Joseph Bouteille&nbsp;</strong>&nbsp; déjà mécanicien au pilotage.</p>



                <div class="wp-block-image"><figure class="aligncenter size-large is-resized"><img loading="lazy" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Canot-essai-en-mer-amiral-ronarch-1024x534.jpg" alt="Canot essai en mer Amiral Ronarc'h" class="wp-image-2063" width="608" height="317"/><figcaption><strong>Canot essai en mer Amiral Ronarc&rsquo;h</strong></figcaption></figure></div>



                <p>Les autres membres étant<strong>&nbsp;Lucien Bossu, &nbsp;Louis Dewitte, Marcel Legrand*, Francis Legrand, Joseph Loyer*, Charles Blondel</strong></p>



                <p>Il faudra attendre 9 ans pour que la station soit à nouveau équipé d&rsquo;un canot. Ce sera l&rsquo;AMIRAL RONARC&rsquo;H qui restera à&nbsp; flot durant 5 ans le temps de construire un nouvel abri.</p>



                <p>* Ces deux lamaneurs&nbsp; périront dans le cadre de leur profession</p>



                <h5><strong>1950&nbsp;&nbsp;</strong></h5>



                <p>L&rsquo;AMIRAL RONARCH a besoin d&rsquo;un nettoyage de coque.&nbsp;Il est demandé aux Ateliers des Chantiers de France de pouvoir amener&nbsp;l&rsquo;amener sur le slip-way afin de&nbsp;procéder au nettoyage.</p>



                <p>Le comité reçoit&nbsp;une facture des ACF&nbsp;&nbsp;de 36291 francs correspondant aux dépenses engagées par les ACF et l&rsquo;immobilisation du slip way.</p>



                <p>Mais un&nbsp; chèque de 50 000 Francs accompagne cette facture couvrant ainsi largement les frais et laissant un don de plus de 10 000 francs.</p>



