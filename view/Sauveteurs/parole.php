        <h1 class="has-text-align-center">Paroles de sauveteurs et sauvés</h1>



        <p></p>



        <p>Quelques mots, quelques phrases émanant d&rsquo;hommes et de femmes qui se sont exprimés en toute simplicité, en toute humilité , comme si les actes accomplis au péril de leur vie étaient l&rsquo;évidence même. <br>Or, ces hommes , ces femmes étaient des héros et ils n&rsquo;en tiraient pas gloire..</p>



        <p class="has-background" style="background-color:#d9dfe2"> </p>



        <div class="wp-block-group has-background" style="background-color:#bbb1b3"><div class="wp-block-group__inner-container">
                <p><strong><a href="https://sauveteurdudunkerquois.fr/tix-2630h/">François Tixier</a></strong> lors du naufrage des<a href="https://sauveteurdudunkerquois.fr/57tro-0250b/"> TROIS SOEURS</a>  en 1857 voulant aller sur le canot a une prémonition sur les risques encourus par Bommelaer, Neuts et Celle</p>



                <p><em>Vous le voyez, dit-il, ils ne veulent pas nous laisser sauver&nbsp; l’équipage&nbsp; – ils vont périr avec lui&nbsp;!&nbsp;</em></p>



                <p>Pendant les opérations de sauvetage après le décès des trois sauveteurs</p>



                <blockquote class="wp-block-quote is-style-default"><p><em>Voilà deux hommes, leur crie-t-il, les laisserons-nous périr comme des chiens&nbsp;? N’est ce pas assez de six victimes&nbsp;? En faudra-t-il huit&nbsp;? Non, non&nbsp;! Affirme-t-il avec énergie&nbsp;!</em></p></blockquote>



                <blockquote class="wp-block-quote is-style-default"><p><em>Non, non&nbsp;!</em></p></blockquote>



                <p>répondent, ces héroïques amis&nbsp;!…</p>



                <blockquote class="wp-block-quote is-style-default"><p><em>Eh bien&nbsp;! reprend<strong>&nbsp;François Tixier</strong>, allons chercher un autre canot, montons-le&nbsp;! Et sauvons-les&nbsp;!</em></p></blockquote>
            </div></div>



