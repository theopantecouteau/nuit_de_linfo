                <blockquote class="wp-block-quote is-style-default"><p><br><em>Allons, garçons, laisserons nous périr ces frères qui comptent sur nous et nous supplient&nbsp;! Puisque Dieu est avec nous et  notre bonne embarcation, marchons !!</em></p></blockquote>
            </div></div>



