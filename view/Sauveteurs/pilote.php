        <p><em>Le premier pilote dunkerquois dont nous trouvons la trace est&nbsp;<strong>Antoine Canny</strong>, pilote de la marine du Roy, avec&nbsp;Jean Bart&nbsp;puis avec le chevalier de Saint-Pol. C&rsquo;était un pilote dit « entretenu » décédé lors d&rsquo;un combat maritime en 1705.</em></p>



        <p><em>Les premières bases du pilotage dunkerquois ouvert aux navires «&nbsp;dits de commerce » sont posées le 10 mars 1728. Le premier chef des pilotes à terre est&nbsp;<strong>Louis Buyck.</strong></em></p>



        <p><em>Il est établi qu&rsquo;une corvette sera en permanence présente à l&rsquo;entrée du port avec 8 pilotes relevés tous les 10 jours. Cette disposition évoluera dans le temps et en 1883, on arrivera à 4 corvettes de pilotage en faction au large du port de Dunkerque pour répondre aux demandes des navires pour être guidés dans les méandres des bancs de sable.</em></p>



        <p><em>Cette présence continuelle en mer sera aussi profitable aux navires en difficulté. Ils trouveront chez les pilotes des hommes prêts à intervenir pour les assister et bien sûr les sortir de situations périlleuses , sauvant ainsi les équipages et, quand cela était réalisable, les navires.&nbsp;</em></p>



        <p><em>Tout comme les lamaneurs qui faisaient partie, à l’origine, de la même entité, «&nbsp;les pilotes lamaneurs&nbsp;», la corporation sera endeuillée, plusieurs fois, par suite d’actes de courage et d’abnégation pour sauver des marins en péril.</em></p>



        <p><em>Déjà, en 1739, dans le premier sauvetage recensé, celui du Saumon Couronné, on apprend que le pilote lamaneur présent à bord est ramené presque mourant au port.</em></p>



        <p><em>On ne peut que mentionner la journée funeste du 28 janvier 1857 où , dans les tentatives pour sauver l’équipage des Trois Sœurs, les pilotes <a href="https://sauveteurdudunkerquois.fr/bom-0234h/">Mathias Bommelaer</a> et Charles Louis Celle périront, avec <a href="https://sauveteurdudunkerquois.fr/neu-2111h/">Gaspard Neuts,</a> sans oublier les pilotes Saveru, Ameloot, Fournier et le matelot pilote Gondol qui laisseront leur vie en voulant sauver leur prochain.</em></p>



        <p><em>En étant de service les pilotes Vanraet, Bommelaer (Louis), Breynaert et Everaert seront victimes de fortunes de mer.</em></p>



        <p><em>Tout comme chez les lamaneurs on remarque des familles liées au sauvetage comme les Bommelaer, Ficquet,&nbsp; Delugny, Brunet (présents sur Gravelines et Dunkerque), Lepretre, Cordier ….</em></p>



        <p><em>Aujourd’hui les pilotes sont toujours présents au sein de la SNSM. Au sein de la station de Dunkerque, dont le&nbsp;&nbsp;patron titulaire est Emmanuel Pelletier, 30% des effectifs sont des pilotes ou des personnels associés au pilotage.</em></p>



