        <h4 class="has-text-align-center"><a href="https://sauveteurdudunkerquois.fr/char-0466h/">Eugène Charet – 1839 -1909</a></h4>



        <p>Présent sur de nombreuses sorties en mer sous l’égide de la SCSN, il a sauvé 172 personnes et 13 équipages. Ses décorations sont à la hauteur du personnage.</p>



