        <h4><em>Un peu d’histoire</em></h4>



        <p><strong>1841</strong>: alors que le port du Havre possède 13 remorqueurs à vapeur, à Dunkerque, un&nbsp; accord est nécessaire entre capitaines pour qu’un vapeur entre au port ou en sorte avec un voilier en remorque.&nbsp;</p>



        <p><strong>1859:</strong>&nbsp;Gaspard Malo et son frère Célestin armateurs et constructeurs de navires exploitent le premier remorqueur à vapeur l&rsquo;ORPHEON. Un premier remorquage est effectué en avril 1859.</p>



        <p><strong>1861:</strong>&nbsp;La chambre de commerce est autorisée à établir un service de remorquage par bateaux à vapeur. L’exclusivité dont jouissait la chambre provoqua l’abandon du remorquage par Gaspard Malo.</p>



        <p><strong>1876:</strong>&nbsp;La chambre de commerce de Dunkerque met ses remorqueurs à la disposition du comité de sauvetage de la SCSN afin de faciliter la sortie du canot quand il doit se porter au secours d&rsquo;un bâtiment en détresse. Les trois remorqueurs de Dunkerque sont dotés d&rsquo;espingoles mises en batteries sur les bastingages et prêtes à envoyer une amarre à un navire en détresse.</p>



        <p>Mais dans les faits, les remorqueurs participent effectivement aux actions de sauvetage depuis 1862.</p>



        <p>Cette année-là, le remorqueur COMMERCE avec le canot de sauvetage et un canot lamaneur sauvera l’équipage du EDWARD EVERETT</p>



        <p><strong>1882:&nbsp;</strong>Les 4 remorqueurs de Dunkerque sont équipés de postes porte-amarres et de secours et le port est équipé avec 30 lignes Torres et 3 lignes Brunel.</p>



        <p><strong>1897:&nbsp;</strong>Le port de Dunkerque perd le monopole du remorquage. La Société dunkerquoise de Remorquage,&nbsp;<strong>Barra et Cie</strong>, considérée comme fondatrice du remorquage professionnel à Dunkerque, est créée.</p>



        <p><strong>1899:</strong>&nbsp;Cette société devient la&nbsp;<strong>Société dunkerquoise de Remorquage et de Sauvetage</strong>&nbsp;. (SDRS). A noter que le sauvetage fait son entrée dans une société à but commercial. Certains naufragés refuseront de faire appel à la SDRS pour ne pas avoir à payer les frais de sauvetage inhérents.<strong>1956:</strong>&nbsp;la SDRS devient la Société de Remorquage et de Sauvetage du Nord.</p>



        <h4>Les sorties de sauvetage du remorquage</h4>



        <p>Depuis 1862, on dénombre&nbsp;<strong>179 sorties en mer&nbsp;</strong>à leur actif dont 78 où ils sont les seuls opérateurs.<br>Ils seront présents sur 65 sorties réalisées par les sociétés de sauvetage institutionnelles (SH et SCSN).</p>



