        <h4 class="has-text-align-center"><a href="https://sauveteurdudunkerquois.fr/sal-3155h/">Thomas Salomont 1888- 1981</a></h4>



        <p>Ce Fort-Philippois réalise son premier sauvetage à l’âge de 11 ans. Cet acte de courage marque le début d’une longue série de dévouement au service des marins en détresse.<br>Mousse en Islande puis novice sur les cap-Hornier, il devient commandant de remorqueur au sein de la SDRS&nbsp; et sauvera&nbsp; 547 personnes pendant sa longue carrière.</p>



        <p>Chevalier de la Légion d’Honneur, du Mérite Maritime à titre exceptionnel faits de guerre, du Mérite Social, Officier du Mérite Maritime, des œuvres humanitaires, Croix de guerre et&nbsp; citation à l&rsquo;ordre de l&rsquo;armée de mer(1940)- Citation à l&rsquo;ordre du régiment(1919)</p>



