        <h2 class="has-text-align-center">Société Centrale de Sauvetage des Naufragés</h2>



        <h1 class="has-text-align-center"><br>Avantages attribués aux sauveteurs en 1914 et de nos jours<br></h1>



        <p></p>



        <h2><strong>Patrons, sous-patrons et canotiers</strong></h2>



        <h2 class="has-text-align-center"><strong>Sorties de canots</strong></h2>



        <p></p>



        <p>Il est alloué à chaque homme ayant pris part à</p>



        <p>une sortie d&rsquo;exercice la somme de 4 fr soit 12,52 €</p>



        <p>une sortie de sauvetage de jour, la somme de 8 fr soit 25,14 €</p>



        <p>une sortie de sauvetage de nuit, la somme de 15 fr soit 47,62 €</p>



        <h2 class="has-text-align-center"><strong>Prix médailles et récompenses pécuniaires</strong></h2>



        <p>Chaque année en Assemblée générale, la Société Centrale distribue aux principaux sauveteurs des prix&nbsp; dont le total s&rsquo;élève à plus de 16.000 francs (soit 50 800 €) ,ainsi qu&rsquo;un certain nombre de médailles d’or.</p>



        <p>Pendant tout le cours de l&rsquo;année, elle décerne des médailles, diplômes et récompenses pécuniaires&nbsp; toutes les fois que des actes de courage et de dévouement en mer lui sont signalés avec les garanties voulues d&rsquo;authenticité.</p>



        <h3><strong>Naissance d’enfant de canotier</strong></h3>



        <p>&nbsp;Pour chaque naissance d&rsquo;enfant de canotier, il est remis à la mère une somme de 20 francs.</p>



        <h3><strong>Femmes et filles de canotiers</strong></h3>



        <p>Tous les ans, au mois de décembre, il est envoyé à chaque station l&rsquo;étoffe d&rsquo;une robe destinée à la femme ou la fille d&rsquo;un canotier.</p>



        <h3><strong>Décès de canotier en activité</strong></h3>



        <p>Lorsqu&rsquo;un canotier en activité, inscrit sur les rôles de la Société Centrale depuis au moins un an, vient à mourir il est alloué :</p>



        <p>1° A chacune des orphelines mineures, un livret de caisse d’épargne de 100 francs</p>



        <p>2° A chacun des orphelins et orphelines âgés de moins de 14 ans, une somme de 50 francs payable en deux fois, à six mois d&rsquo;intervalle.</p>



        <p>3° A ses ascendants, lorsque ce décès les a privés de leur soutien, un secours d&rsquo;importance variable fixé par le Conseil d&rsquo;administration.</p>



        <h3><strong>Mariage de canotier ou fille de canotier</strong></h3>



        <p>&nbsp;&#8211; Lorsqu&rsquo;un canotier se marie, ou lorsque la fille d&rsquo;un canotier épouse un marin, il est fait don à la mariée d&rsquo;une armoire. Pour cette donation, s&rsquo;adresser à la Société de Secours aux Familles des Marins Français naufragés.</p>



        <h3><strong>Gratification de congédiement</strong></h3>



        <p>&#8211; Les canotiers, sont rayés des rôles à 60 ans, ou plus tôt si l&rsquo;état de leur santé l&rsquo;exige. A ce moment, sur la proposition du Comité local, il peut leur être alloué une gratification non renouvelable de 60 francs s’ils ont au moins 15 ans de service, de 40 francs&nbsp; s&rsquo;ils en ont 10, et de 30 francs&nbsp; s&rsquo;ils en ont 5.</p>



        <h3><strong>Secours aux canotiers et leurs familles</strong></h3>



        <p>Chaque année, la Société Centrale accorde des secours en argent&nbsp; à un certain nombre de veuves de ses canotiers, ainsi qu&rsquo;à un certain nombre de ses canotiers tant en activité qu&rsquo;en retraite, pour motifs exceptionnels.</p>



        <h3><strong>Canotiers décorés de la Légion d’Honneur</strong></h3>



        <p>&#8211; La Société Centrale accorde, à tous ses patrons, sous-patrons et canotiers décorés de la Légion d&rsquo;honneur pour faits de sauvetage en mer, une pension annuelle de 100 Francs</p>



        <h2 class="has-text-align-center"><u><strong>Avantages pour les patrons</strong></u></h2>



        <h3><strong>Solde</strong></h3>



        <p>&#8211; Les patrons reçoivent un traitement de 200 francs . Exception est faite pour les patrons de quelques embarcations d&rsquo;un modèle réduit, dont la solde est fixée par des décisions particulières.</p>



        <h3><strong>Gratification de bon entretien</strong></h3>



        <p>&#8211; A la suite des visites annuelles, et sur la proposition de l&rsquo;Inspecteur, il peut être alloué au patron&nbsp; une gratification de bon entretien.</p>



        <h3><strong>Cirés de sauvetage.</strong></h3>



        <p>&#8211;&nbsp; Les patrons exerçant la profession de pêcheurs en pleine mer reçoivent tous les deux ans un ciré de sauvetage doublé de kapok.</p>



        <h3><strong>Costumes de mousse</strong></h3>



        <p>&#8211; Les fils et petits-fils de patron, en activité ou en retraite, qui embarquent comme mousses, reçoivent six mois après leur premier embarquement, un costume de mousse comprenant : un béret, un jersey, un pantalon, une paire de chaussures et deux paires de chaussettes.</p>



        <h3><strong>Baromètres</strong></h3>



        <p>&#8211; Les patrons de canots reçoivent un baromètre, qu&rsquo;ils doivent remettre à leur successeur quand ils cessent leurs fonctions.</p>



        <h3><strong>Retraite</strong></h3>



        <p>&#8211;&nbsp; Les patrons sont rayés des rôles à 65 ans, ou plus tôt si l&rsquo;Inspecteur juge que l&rsquo;état de leur santé l&rsquo;exige. Il leur est accordé alors une pension de 200 francs s&rsquo;ils ont 15 ans de service, soit comme patron, soit comme canotier, et de 150 francs s&rsquo;ils ont 10 ans. Toutefois, le montant de la pension de retraite ne peut excéder la solde d&rsquo;activité.</p>



        <p>Les six patrons en retraite les plus âgés reçoivent un supplément de pension de 100 francs.</p>



        <h3><strong>Veuves de patrons</strong></h3>



        <p>&#8211; Toute veuve de patron décédé, soit en activité, soit en retraite, et ayant figuré au moins 15 ans sur les rôles de la Société Centrale, reçoit une pension de 70 francs.</p>



        <h2 class="has-text-align-center"><strong><u>Avantages pour les sous-patrons</u></strong></h2>



        <h3><strong>Solde</strong></h3>



        <p>&#8211; Les sous-patrons reçoivent un traitement de 70 francs.&nbsp;</p>



        <h3><strong>Cirés de sauvetage.</strong></h3>



        <p>&#8211; Les sous-patrons exerçant la profession de pêcheurs en pleine mer reçoivent tous les deux ans un ciré de sauvetage doublé de kapok.</p>



        <h3><strong>Costumes de mousse</strong></h3>



        <p>&#8211;&nbsp; Les fils et petits-fils de sous-patron, en activité ou en retraite, qui embarquent comme mousses, reçoivent six mois après leur premier embarquement, un costume de mousse comprenant : un béret, un jersey, un pantalon, une paire de chaussures et deux paires de chaussettes.</p>



        <h3><strong>Retraites</strong></h3>



        <p>&#8211; Les sous-patrons sont rayés des rôles à 65 ans, ou plus tôt si l&rsquo;inspecteur juge que l&rsquo;état de leur santé l&rsquo;exige. Il leur est accordé alors une pension de 70 francs s&rsquo;ils ont 15 ans de service, soit comme sous-patron, soit comme canotier, et de 50 francs s&rsquo;ils ont 10 ans.</p>



        <p>Les six sous-patrons en retraite les plus âgés reçoivent un supplément de pension de&nbsp;&nbsp; 50 francs.</p>



        <h3><strong>Veuves de sous-patrons</strong></h3>



        <p>&#8211; Toute veuve de sous-patron décédé, soit en activité, soit en retraite, et ayant figuré au moins 15 ans sur les rôles de la Société Centrale, reçoit une pension de 60 francs.</p>



