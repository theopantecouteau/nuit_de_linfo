        <h1 class="has-text-align-center"><strong>Islandais et sauveteurs</strong></h1>



        <h3 class="has-text-align-center">20 équipages et 65 personnes sauvées (soit environ 430 personnes)</h3>



        <blockquote class="wp-block-quote"><p>Dans l’histoire des sauveteurs du dunkerquois, les islandais ont une part non négligeable dans le nombre&nbsp;de personnes sauvées,&nbsp;sauvetages effectués dans des conditions bien souvent épouvantables.</p><p>Sur un siècle, de 1835 à 1935, les marins de 27 goélettes, au moins, ont été&nbsp;sauvés par des équipages d’autres goélettes ou, parfois,&nbsp;grâce au courage d’un seul des leurs et ce dans des conditions climatiques extrêmes.</p><p>L’histoire des islandais a été&nbsp; racontée et écrite par des auteurs qui méritent votre attention. Vous trouvez en bas de cette page des liens vers ces ouvrages incontournables. Cette page retrace et rend hommage au courage de ces marins islandais pour qui les notions de courage, d’abnégation et de solidarité maritime ne sont pas des vains mots.*</p></blockquote>



        <figure class="wp-block-image size-full"><img src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Carte-naufrages-islande.png" alt="Carte naufrages islande" class="wp-image-1732"/><figcaption><strong>Carte naufrages Islande</strong></figcaption></figure>



