        <h2>100 ans de sauvetages</h2>



        <p><strong>1835&nbsp; &nbsp;</strong></p>



        <p>La goélette BAROM N’DAR sauve l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/35jeu-0760b/">JEUNE CAROLINE</a></p>



        <p><br><strong>1844&nbsp;</strong></p>



        <p>Le même BAROM N’DAR sauve l’équipage de la goélette&nbsp;<a href="https://sauveteurdudunkerquois.fr/44hec-0766b/">HECLA</a></p>



        <p>Le sloop AIGLE recueille en baie Rackefjord l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/44jeu-0767b/">JEUNE HYPPOLITE</a>&nbsp;(excepté&nbsp; le tonnelier du bord)</p>



        <p><strong>1859</strong></p>



        <p>Le&nbsp;marin&nbsp;dunkerquois&nbsp;<strong>Jean Pierre Joseph Evrard</strong>&nbsp;sauve, dans des circonstances pour le moment inconnues,&nbsp;en mer d’Islande, l’équipage du lougre les<a href="https://sauveteurdudunkerquois.fr/59deu-0700b/">&nbsp;DEUX LOUISES</a>&nbsp;de Fécamp</p>



        <p>Des marins dunkerquois et boulonnais sauvent, dans des circonstances pour le moment inconnues, l’équipage de la Bisquine&nbsp;<a href="https://sauveteurdudunkerquois.fr/59ecl-0778b/">ECLAIR</a>&nbsp;perdue en mer d’Islande</p>



        <p>Les dunkerquois <strong>A. E. Vivet</strong>, <strong>L. F. Maes</strong>, <strong>F. D. Tjaeckx</strong>&nbsp; du&nbsp; CELESTE ROSE sauvent 11 marins du lougre&nbsp;<a href="https://sauveteurdudunkerquois.fr/2679-2/">SAINT PAUL</a></p>



        <p><strong>1869</strong></p>



        <p>L’équipage de la&nbsp; CAMILLE, bien qu’ayant son pont dévasté par la tempête, arrive, malgré tout,&nbsp; à sauver l’équipage&nbsp; de la<a href="https://sauveteurdudunkerquois.fr/69pie-0947b/">&nbsp;SAINTE PIERRAISE</a>&nbsp; de Saint-Malo</p>



        <p><strong>Avant 1870&nbsp;&nbsp;</strong></p>



        <p>&nbsp;Sauvetage de 18 personnes en Islande par <strong><a href="https://sauveteurdudunkerquois.fr/ree-2361h/">Emile Rees</a></strong> (Source dossier Légion d’Honneur)</p>



        <p><strong>1876</strong></p>



        <p>La goélette dunkerquoise FRANCE, commandée par le capitaine <strong>Admont </strong>sauve l’équipage de la Goélette&nbsp;<a href="https://sauveteurdudunkerquois.fr/76mar-0791b/">MARTHA.</a></p>



        <p><strong>1888</strong></p>



        <p>La goélette dunkerquoise FIANCEE sauve  l’équipage de la  goélette <a href="https://sauveteurdudunkerquois.fr/88mar-0626b/">MARIE VALENTINE</a></p>



        <p><strong>1891</strong></p>



        <p>Seul contre les éléments, le Fort-Philippois <a href="https://sauveteurdudunkerquois.fr/gen-1206h/"><strong>Pierre Gens</strong></a> plonge dans les eaux glacées pour mettre en place un va-et-vient avec la côte et sauver l’équipage de la<a href="https://sauveteurdudunkerquois.fr/91pri-0407b/">&nbsp;PRIMEVERE&nbsp;</a>dont il fait partie. <br><strong>Pierre Gens</strong> sera élevé à la dignité de Chevalier de la Légion d’Honneur</p>



        <p>1<strong>892</strong></p>



        <p>L’équipage de la CHARMEUSE sauve les 18 hommes de l’<a href="https://sauveteurdudunkerquois.fr/92eto-0386b/">ETOILE DU MARIN</a>&nbsp;au large des îles Féroé</p>



        <p><strong>1895</strong></p>



        <p><strong>Clovis Carru</strong>, second à bord de la goélette&nbsp;<a href="https://sauveteurdudunkerquois.fr/95jea-0467b/">JEANNE</a> sauve, en mettant en place un va-et-vient&nbsp; avec la terre, son équipage à l’exception d’un mousse. Deux hommes périront de froid pendant la nuit</p>



        <p><strong>1901</strong></p>



        <p>L’équipage de la goélette ALERTE sauve l’équipage de la goélette&nbsp;&nbsp;<a href="https://sauveteurdudunkerquois.fr/01sou-0552b/">SOUVEREINE</a></p>



        <p><strong>1902</strong></p>



        <p>L’armement de la JEUNE LEONIE, au large de Myre bank,&nbsp; sauve 11 membres de l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/02cha-0603b/">CHARMEUSE</a>. <br> <em>Comme il n’y a pas assez de place dans le canot de sauvetage, on tire à la courte-paille. Le mousse du bord est désigné avec deux autres marins. Le papa présent prend la place de son fils et lui donne un gâteau pour…</em></p>



        <p>10 membres de l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/02per-0604b/">PERLE</a>&nbsp;sont sauvés par l’armement de la<a href="https://www.sauveteurdudunkerquois.fr/02SAI-0628B/">&nbsp;</a>MARTHA</p>



        <p>L’équipage de la goélette PAULINE sauve l’équipage de la goélette&nbsp;<a href="https://sauveteurdudunkerquois.fr/02sai-0628b/">SAINT PIERRE</a></p>



        <p>Les équipages de la SAINTE ESPERANCE et de l’EUGENIE sauvent l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/02lab-0627b/">LABORIEUSE</a></p>



        <p><strong>1903</strong></p>



        <p>L’équipage du lougre de pêche&nbsp;<a href="https://sauveteurdudunkerquois.fr/03jeu-0541b/">JEUNE HENRIETTE</a>&nbsp;est sauvé par le sloop MELINA.</p>



        <p><strong>1905</strong></p>



        <p>Sauvetage des 25 membres de l’équipage de la&nbsp;<a href="https://sauveteurdudunkerquois.fr/05sai-0964b/">SAINTE MARIE</a>&nbsp;par&nbsp;<strong><a href="https://sauveteurdudunkerquois.fr/vand-3095h/">Vandenbussche</a></strong></p>



        <p><strong>1906</strong></p>



        <p>Les équipages des PECHEUSE et LOUISE sauvent l’équipage du&nbsp;<a href="https://sauveteurdudunkerquois.fr/06reg-0468b/">REGINA ET COLI</a></p>



        <p><strong>1910</strong></p>



        <p>Les goélettes HERMINE et FIANCEE sauvent l’équipage de la goélette&nbsp;<a href="https://sauveteurdudunkerquois.fr/10cza-0624b/">CZARINE</a></p>



        <p><strong>1912</strong></p>



        <p>L’équipage de la <a href="https://sauveteurdudunkerquois.fr/12fre-0455b/">FREGATE</a> est sauvé par l’armement de la MARGUERITE</p>



        <p><strong>1914</strong></p>



        <p>NOTRE DAME DE LA MER&nbsp;assure le&nbsp;<strong>&nbsp;</strong>sauvetage de l&rsquo;&nbsp;<a href="https://sauveteurdudunkerquois.fr/14eug-0965b/">EUGENIE</a></p>



        <p><strong>1924</strong></p>



        <p>L’<a href="https://sauveteurdudunkerquois.fr/24aug-0716b/">AUGUSTA</a>&nbsp;est sur le point de couler.&nbsp;<strong>Adolphe Carru</strong>, faisant valoir sa position de célibataire, se jette dans les eaux glacées pour tenter d’établir un va-et-vient. Les jambes prises dans le filin il périra noyé.&nbsp;<strong>Lemaire</strong>, second du navire,&nbsp; se jette à son tour et réussit à établir un va-et- vient avec la terre</p>



        <p><strong>1934</strong></p>



        <p>Dans des conditions effroyables, les équipages des JACQUES BERTHE et PASCALE sauvent l’équipage danois du&nbsp;<a href="https://sauveteurdudunkerquois.fr/34ste-0524b/">WILLIAM STEPHENSON</a></p>



        <p><strong>1935</strong></p>



        <p>Le matelot&nbsp;&nbsp;<strong>Adolphe Laurent&nbsp;</strong>se jette dans les eaux glacées et arrive, après une nage exténuante, à établir un va-et-vient pour sauver l’équipage du&nbsp;<a href="https://sauveteurdudunkerquois.fr/35lie-0493b/">LIEUTENANT BOYAU</a>&nbsp;dont il faisait partie. 3 membres d’équipage ne pourront être sauvés.</p>



