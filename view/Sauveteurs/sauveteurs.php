                <p>Bienvenue sur le site des sauveteurs du dunkerquois. Ce site rend hommage aux femmes, hommes et enfants qui ont réalisé des actes de sauvetages en milieu aquatique.<br>Ces sauveteurs, habitants du dunkerquois (de Bray-Dunes à Grand-Fort-Philippe), ont participé à plus de 900 sauvetages en mer et plus de 1100 sauvetages individuels. Œuvrant avec courage, abnégation et souvent au mépris du risque ils méritent amplement que leurs actes soient pérennisés. </p>



                <p class="has-text-align-center">La citation ci-dessous, signée <a href="https://sauveteurdudunkerquois.fr/bosa-0257h/">Arsène Bossu</a>, à un journaliste du Grand Echo du Nord de la France en 1932, résume à elle seule l&rsquo;état d&rsquo;esprit des sauveteurs du Dunkerquois.</p>
