        <h1 class="has-text-align-center"><strong>Douaniers et sauveteurs</strong></h1>



        <figure class="wp-block-image"><img src="https://www.sauveteurdudunkerquois.fr/gallery_gen/1a99cc4ba8087cf2f5bfba299f798a8c_2400x1424.jpg" alt=""/></figure>



        <p>Si les pilotes et les lamaneurs représentent la cheville ouvrière du sauvetage en mer institutionnalisé,&nbsp; les douaniers qui sont les acteurs incontournables du sauvetage individuel participent eux aussi&nbsp; aux&nbsp; sauvetages des navires au large du Dunkerquois.</p>



        <p>Présents dans les ports et sur tout le littoral pour empêcher d’éventuels déchargements illégaux, embarqués à bord de leurs pataches (équivalent des vedettes des douanes d’aujourd’hui), les douaniers armaient également les stations porte-amarre de Cayenne&nbsp;(localisation estimée du côté du pilotage de nos jours) &#8211; Risban Ouest &#8211; Leffrinckoucke &#8211; Mardyck&nbsp;et Grand-Fort-Philippe. Ces stations étaient destinées à porter secours aux bateaux en perdition à proximité&nbsp;de la côte en mettant en place un système de va-et-vient et bouée culotte à l’aide d’un&nbsp; fusil ou d’un&nbsp;canon lance amarre.</p>



        <p>Leur présence sur les quais des ports du dunkerquois ont permis de sauver nombre de vies de marins, d’ouvriers du port, d’enfants…. qui pour des raisons variées ont chu dans les eaux portuaires ou parfois dans les canaux.</p>



        <p>Ainsi sur 1027 sauvetages individuels recensés entre 1824 – 1904, ils ont&nbsp; permis de sauver plus de 546 personnes.</p>



        <p>Ils étaient équipés dans les années 1860 de&nbsp;lignes Torrès&nbsp;ou de&nbsp;gaffes Legrand.</p>



        <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="673" height="326" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Ligne-Torres.png" alt="Ligne Torrès" class="wp-image-1633" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Ligne-Torres.png 673w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Ligne-Torres-300x145.png 300w" sizes="(max-width: 673px) 100vw, 673px" /><figcaption>Ligne Torres</figcaption></figure></div>



        <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="679" height="140" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Gaffe-legrand.png" alt="Gaffe Legrand" class="wp-image-1631" srcset="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Gaffe-legrand.png 679w, https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Gaffe-legrand-300x62.png 300w" sizes="(max-width: 679px) 100vw, 679px" /><figcaption>Gaffe Legrand</figcaption></figure></div>



        <p>Ces moyens de secours, relativement encombrants, ont été rapidement supplantés, dans les années 1870, par la ligne Brunel, inventé par l&rsquo;ancien lieutenant des douanes Brunel,&nbsp;qui présentait l’avantage de pouvoir être portée, dans son étui, au ceinturon des douaniers en faction. On en retrouve parfois l’usage en complément de la ligne Torrès, pour finaliser un sauvetage opéré par ligne Brunel.</p>



        <div class="wp-block-image"><figure class="aligncenter size-full"><img loading="lazy" width="199" height="147" src="https://sauveteurdudunkerquois.fr/wp-content/uploads/2021/09/Douanes-Ligne-Brunel.png" alt="Ligne Brunel" class="wp-image-1632"/><figcaption><strong>Ligne Brunel</strong></figcaption></figure></div>



        <p>Les douaniers étaient aussi présents sur des sauvetages depuis la côte ou le port. On les retrouve présents sur&nbsp;:</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1810</strong>&nbsp;Sauvetage du&nbsp;QUEEN ELIZABETH</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp; &nbsp;1853</strong>&nbsp;Sauvetage du&nbsp;PASSE BALLE. Équipage sauvé par la patache des douanes</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp;1859</strong>&nbsp;Sauvetage du&nbsp;DEUX ERNESTINES</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1868</strong>&nbsp;Incendie du&nbsp;BILLOW</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp;1871</strong>&nbsp;Sauvetage du&nbsp;&nbsp;CATHERINA-CHRISTINA, 7 hommes sauvés 1 sauveteur décédé</p>



        <p>&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp;&nbsp;1880&nbsp;</strong>Sauvetage du Lougre&nbsp;DIEU PROTEGE LA FRANCE, 4 personnes sauvées</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Sauvetage de la goélette&nbsp;SAINT JOSEPH&nbsp;, le bateau arrive à se déséchouer</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>&nbsp;&nbsp;1881&nbsp;</strong>Sauvetage du vapeur gravelinois&nbsp;EMILE,&nbsp; 5 personnes sauvées</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>&nbsp;&nbsp;1882</strong>&nbsp;Sauvetage de la goélette&nbsp;PHOEMIE DE GOOLE, 4 hommes sauvés</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1883</strong>&nbsp;Sauvetage du sloop de pêche&nbsp;ARMAND ET ARMANDE,&nbsp; personnes sauvées</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1897</strong>&nbsp;Sauvetage du trois-mâts norvégien&nbsp;MANITOBA. Equipage sauvé</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp;1900&nbsp;</strong>Sauvetage du&nbsp;SAINT JOSEPH&nbsp;&#8211; Lamaneur N°12,&nbsp; 2 hommes sauvés &#8211; 1 mort</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;Sauvetage du&nbsp;EUGENE MARIE, 2 personnes sauvées</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Sauvetage&nbsp;Bateau de pêche N°328, 2 hommes sauvés</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<strong>&nbsp;1906</strong>&nbsp;Sauvetage de&nbsp;l&rsquo;HELENE&nbsp;face à Mardyck, 8 personnes sauvées</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1923</strong>&nbsp;Sauvetage vapeur&nbsp;ERIKA, 9 personnes sauvées depuis la plage du Clipon</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1933</strong>&nbsp;Sauvetage du&nbsp;DYCK</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1938</strong>&nbsp;Sauvetage du yacht&nbsp;RACHEL. Equipage sauvé</p>



        <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>1991</strong>&nbsp;Présence sur le naufrage du&nbsp;REMY MATTHIEU</p>



        <figure class="wp-block-image"><img src="https://www.sauveteurdudunkerquois.fr/gallery_gen/40e0bdbbc7a5a2c8ce8bb89555592599_2325x1406.625.jpg" alt=""/></figure>



        <p>Ils n’hésitent pas non plus à prêter main forte aux canots de sauvetage.&nbsp;</p>



        <p><strong>1949</strong>&nbsp;le patron des douanes&nbsp;<strong>François Legrand&nbsp;</strong>vient volontairement aider l’équipage de l’AMIRAL RONARCH lors du naufrage du PRINCESSE ASTRID.</p>



        <p><strong>1952</strong>&nbsp;4 douaniers prêteront main forte à l’équipage de l’AMIRAL RONARCH pour porter secours à la vedette de la Douane Maritime, PATRON VANHILLE au sec dans la fosse de Mardyck.</p>



        <p>Il est par contre difficile d’identifier précisément les douaniers car, dans la majorité des cas, le prénom ne figure pas sur les rapports. Comme il y avait, tout comme pour les pilotes, lamaneurs ou&nbsp; pêcheurs, des familles de douaniers les comptages de sauvetages réalisés&nbsp;sur ces occurrences sont très aléatoires.</p>



        <p>On peut toutefois citer :</p>



        <p><strong>1879</strong>&nbsp; Le douanier&nbsp;<strong>Richard Queva&nbsp;</strong>qui recevra une médaille de sa Majesté&nbsp;la Reine d&rsquo;Angleterre pour son action remarquable lors de l’incendie du BILLOW en 1868 et&nbsp;aurait, si on est sur le même personnage, sauvé en 1879, avec une ligne Torrès, un marin avec l’aide du douanier Sonneville.</p>



        <p><strong>1871</strong>&nbsp; Le lieutenant des douanes&nbsp;<strong>Guericy&nbsp;</strong>qui permettra, en tirant une seule flèche Delvigne, de mettre en place un va-et-vient et de sauver 7 personnes lors du sauvetage du CATHARINA CHRISTINA, qui verra la mort de François Tixier.</p>



