        <h1 class="has-text-align-center"><strong>Lamaneurs et sauveteurs</strong></h1>



        <h1 class="has-text-align-center"><strong>7&nbsp;équipages et 108 personnes sauvées</strong></h1>



        <h3 class="has-text-align-center">58 lamaneurs sauvés &#8211; 46 lamaneurs décédés&nbsp;</h3>



        <p>Au XIXe et début XXe siècle, les lamaneurs, regroupés par 4 à 5 dans un canot avec un patron, étaient sur rade&nbsp; afin d&rsquo;aider aux manœuvres des bâtiments (prise de remorque, amarrage etc.) et de leur porter des vivres et rafraîchissements, voire récupérer des passagers à bord de navires ayant un trop fort tirant d&rsquo;eau pour les amener à quai.</p>



        <p>Les mouvements se faisaient sur des canots du type de celui en illustration. Il y avait une concurrence entre les canots, qui étaient tous numérotés mais dont on n’a parfois gardé que le nom de baptême.</p>



        <p>En attente de clients potentiels devant le port, (ils disaient « être en drogue »), ils étaient souvent les premiers, avec les pilotes et les équipages de remorqueurs (à partir de 1860), à intervenir en cas de naufrage. Nombre de leurs&nbsp;interventions concernent d&rsquo;ailleurs des canots lamaneurs comme eux.</p>



        <p>Entre 1822 et 1829 ils ont participé avec leurs canots à 43 opérations de sauvetage&nbsp;ou d&rsquo;assistance.</p>



        <p>Ensuite la motorisation des matériels, le développement des moyens de communication moderne ont changé l&rsquo;organisation de cette fonction essentielle au fonctionnement des mouvements&nbsp;portuaires, réduisant ainsi leurs interventions directes.</p>



        <p>Remarquons que,&nbsp;depuis la création de la Société Humaine en 1834,&nbsp;nombres de lamaneurs ont&nbsp; fait partie des armements des canots de sauvetage.</p>



        <p>Cette profession était et est toujours familiale. On est souvent&nbsp;lamaneur de père en fils.</p>



        <p>Si les lamaneurs d&rsquo;aujourd&rsquo;hui ne tirent plus sur le&nbsp;bois (les avirons), le métier n&rsquo;en est pas moins dangereux. Le dernier lamaneur mort en service est Gérald Elleboode en 1995 juste après son oncle René en 1992&nbsp;.</p>



        <p>Des familles ont laissé leur marque dans le monde du sauvetage avec les patrons ou sous-patrons :&nbsp;&nbsp;<strong><a href="https://sauveteurdudunkerquois.fr/bosa-0257h/">Bossu</a>, <a href="https://sauveteurdudunkerquois.fr/etc-3403h/">Etcheverry</a>, <a href="https://sauveteurdudunkerquois.fr/ree-2361h/">Rees</a>, <a href="https://sauveteurdudunkerquois.fr/tay-2602h/">Taylor</a>,</strong>&nbsp;sans oublier les&nbsp;<strong><a href="https://sauveteurdudunkerquois.fr/pat003/">Bateman</a>, Eggerickx(s), Houvenaghel, Loyer(z), Zonnekindt&nbsp;</strong>et bien d&rsquo;autres qui ont fourni les canotiers indispensables à la bonne marche des canots de sauvetage.&nbsp;</p>



        <p>Le dernier patron </p>



        <p>Le dernier patron lamaneur  du JEAN BART avait pour nom<strong>&nbsp;<a href="https://sauveteurdudunkerquois.fr/bosd-4847h/">Didier Bossu</a></strong></p>



        <p></p>



        <p>En 1947, deux anciens patrons lamaneurs furent sollicités par la chambre de commerce pour réorganiser le service, ils créent donc une association de fait. En 1970, cette association regroupe 24 personnes, elle se transforme alors en société coopérative ouvrière de production anonyme, ce qui est toujours la structure de la société aujourd’hui avec comme président Alexandre&#8230;&nbsp;&nbsp;<strong>Bossu</strong>&nbsp;qui s&rsquo;inscrit dans la continuité familiale.</p>



        <p>Deux citations d&rsquo;<strong><a href="https://sauveteurdudunkerquois.fr/bosa-0257h/">Arsène Bossu</a></strong>&nbsp;résument l&rsquo;état d&rsquo;esprit de ces marins, prêts à braver tous les dangers pour sauver leurs semblables :</p>



        <blockquote class="wp-block-quote"><p><em>Si un homme tombe à la « baille »,</em></p><p><em>est-ce qu&rsquo;on ne va pas le chercher ?</em></p><p><strong>Il n&rsquo;y a à cela aucun mérite.</strong></p></blockquote>



        <p>et celle-ci, dans laquelle, pour moi, chaque&nbsp;mot&nbsp;a son importance</p>



        <p><em>J&rsquo;étais lamaneur, donc canotier de profession.</em></p>



        <blockquote class="wp-block-quote"><p><strong>On m&rsquo;a fait l&rsquo;honneur</strong>&nbsp;<em>de me donner une place dans le canot de sauvetage, c&rsquo;était donc à mes camarades et à&nbsp;</em><strong>moi qu&rsquo;était offerte l&rsquo;occasion</strong>&nbsp;<em>d&rsquo;aller au secours des équipages en perdition.</em></p></blockquote>



