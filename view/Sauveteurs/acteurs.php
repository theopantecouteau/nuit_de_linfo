        <h2>Qui sont les sauveteurs</h2>



        <p>Les sauveteurs en mer, en toute logique, avec toutefois de rares exceptions, &nbsp;sont&nbsp;issus du monde maritime: pilotes, lamaneurs, gréeurs, marins de commerce, des feux-flottants, pêcheurs, etc.&nbsp;</p>



        <p><strong>Les patrons et sous-patrons de Dunkerque</strong>&nbsp;ont, en général issus,&nbsp; des professions de pilote et de patron-lamaneur.&nbsp;Des exceptions avec&nbsp;Charles Lauwick&nbsp;tonnelier à bord des goélettes islandaises, Francois Tixier&nbsp;l’emblématique entrepreneur de lestage,&nbsp;Jules Aimé Conseil officier au long cours et officier de port</p>



        <p><strong>Les patrons et sous-patrons de Gravelines</strong>&nbsp;ont des professions de pilotes du port et de patrons pêcheurs locaux.</p>



        <p><strong>Les patrons et sous patron de Fort-Mardyck&nbsp;</strong>sont&nbsp;&nbsp;issus du monde de la pêche.</p>



        <p>Avec plus de 1300 personnes sauvées en moins d&rsquo;un siècle il convient de&nbsp;ne pas oublier les sauveteurs présents sur les quais, les plages, près des watergands etc.…&nbsp;. Pour ces derniers on constate une forte présence des douaniers mais également d&rsquo;éclusiers, et de personnes de toutes origines du médecin au journalier en passant par les enfants sauveteurs qui ne sont pas les derniers à faire preuve de courage.</p>



