        <p><strong>Quel dunkerquois n&rsquo;a jamais lu les plaques commémoratives  apposées sur notre Leughenaer?</strong></p>
        <p>


        <p></p>
        <p></p>


        </p>
        <p>Sur cette page on trouvera les sauveteurs décorés de  la<strong> Légion  d&rsquo;Honneur</strong> et ou du <strong>Mérite Maritime</strong> pour services rendus.</p>
        <p>


        <p></p>
        <p></p>


        </p>
        <p>La deuxième plaque apposée par les soins de la <a href="https://www.sauveteurdudunkerquois.fr/Naissance-du-sauvetage-dunkerquois/">Société Humaine de Dunkerque</a> rend hommage aux trois pilotes décédés dans le naufrage des TROIS SOEURS. La deuxième partie est un hommage à Jean-François Tixier sauveteur émérite des premières années de la société et qui perdra la vie en voulant sauver son prochain.</p>
        <p>


        <p></p>
        <p></p>


        </p>
        <p>La station de Gravelines a aussi des sauveteurs qui ont été remerciés par l&rsquo;attribution de la Légion d&rsquo;Honneur.</p>
        <p>


        <p></p>
        <p></p>


        </p>
        <p>Les légionnaires et titulaires du Mérite Maritime ont souvent été patron ou sous-patron des canots de sauvetage, patron de remorqueur, etc.</p>
        <p>


        <p></p>
        <p></p>


        </p>
        <p>Ces fonctions et les défraiements qui leur sont liés ont été définis dès la création de la <a href="https://www.sauveteurdudunkerquois.fr/La-SCSN/">SCSN</a>. <a href="https://www.sauveteurdudunkerquois.fr/gallery/r%C3%B4le%20des%20patrons%20et%20sous%20patrons%201866.pdf">Extrait du réglement ici .<br /></a></p>
